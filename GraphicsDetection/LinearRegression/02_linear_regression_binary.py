# ============================================================
# 02. Linear Regression с бинаризацией
#
# Что добавлено:
# - бинаризация изображения перед регрессией
# - повышает стабильность обнаружения линии
#
# Как это работает:
# 1. Получаем кадр с камеры в оттенках серого
# 2. Применяем бинаризацию: все пиксели темнее порога становятся чёрными (0),
#    все светлее - белыми (255)
# 3. Ищем линию только среди белых пикселей (255,255)
# 4. Это даёт более чёткий результат при плохом освещении
# ============================================================

import time
from media.sensor import *
from media.display import *
from media.media import *

# Порог бинаризации: пиксели со значением 0-100 станут чёрными,
# остальные - белыми. Подходит для поиска тёмных линий на светлом фоне.
THRESHOLD = (0, 100)

# Инициализация камеры (сенсора)
sensor = Sensor()
sensor.reset()  # Сброс настроек камеры к заводским
# Установка разрешения: 640x480 пикселей (стандарт VGA)
sensor.set_framesize(width=640, height=480)
# Формат изображения: оттенки серого (только яркость, без цвета)
sensor.set_pixformat(Sensor.GRAYSCALE)

# Инициализация дисплея
# Display.ST7701 - тип экрана, to_ide=True - передача в IDE для отладки
Display.init(Display.ST7701, to_ide=True)
MediaManager.init()  # Инициализация менеджера медиа-ресурсов

sensor.run()  # Запуск потока данных с камеры
clock = time.clock()  # Таймер для подсчёта FPS (кадров в секунду)

# Основной бесконечный цикл обработки кадров
while True:
    clock.tick()  # Начало отсчёта времени для текущего кадра
    img = sensor.snapshot()  # Получение текущего кадра с камеры

    # === БИНАРИЗАЦИЯ ===
    # Преобразуем изображение в чёрно-белое по порогу
    # Все пиксели в диапазоне THRESHOLD станут либо 0 (чёрный), либо 255 (белый)
    # Это убирает шумы и упрощает дальнейшую обработку
    img.binary([THRESHOLD])

    # === ЛИНЕЙНАЯ РЕГРЕССИЯ ===
    # Ищем линию среди белых пикселей (255,255)
    # get_regression() строит наилучшую прямую через найденные пиксели
    # Метод наименьших квадратов используется для вычисления линии
    line = img.get_regression([(255, 255)])

    if line:
        # Если линия найдена - рисуем её на изображении
        # color=127 - серый цвет (из 255), thickness=4 - толщина 4 пикселя
        img.draw_line(line.line(), color=127, thickness=4)

    # Вывод изображения на экран
    Display.show_image(img)
    
    # Вывод производительности (кадры в секунду)
    print("FPS:", clock.fps())
