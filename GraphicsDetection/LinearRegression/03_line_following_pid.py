# Описание:
# Пример использования линейной регрессии для следования по линии.
# Скрипт вычисляет отклонение (error) центра линии от центра экрана,
# что является основой для PID-регулятора в робототехнике.
#
# Используется:
# - Sensor (камера)
# - Display (экран)
# - MediaManager (управление медиа)
#
