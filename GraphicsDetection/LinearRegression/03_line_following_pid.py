# ============================================================
# 03. Простой line following (основа автопилота)
#
# Идея:
# - находим линию методом линейной регрессии
# - считаем центр найденной линии
# - сравниваем с центром экрана
# - получаем ошибку (error) - отклонение от центра
#
# Это основа для управления роботом:
# - если error > 0 - линия справа, нужно повернуть вправо
# - если error < 0 - линия слева, нужно повернуть влево
# - если error = 0 - линия по центру, движемся прямо
#
# В будущем ошибка используется в PID-регуляторе для плавного
# управления моторами робота.
# ============================================================

import time
from media.sensor import *
from media.display import *
from media.media import *

# Порог бинаризации: ищем тёмные пиксели (0-100 из 255)
# Подходит для чёрной линии на белом фоне
THRESHOLD = (0, 100)

# Ширина изображения в пикселях
IMG_WIDTH = 640

# Инициализация камеры (сенсора)
sensor = Sensor()
sensor.reset()  # Сброс настроек камеры к заводским
# Установка разрешения: 640x480 пикселей
sensor.set_framesize(width=640, height=480)
# Формат: оттенки серого (только яркость, без цвета)
sensor.set_pixformat(Sensor.GRAYSCALE)

# Инициализация дисплея
# Display.ST7701 - тип экрана, to_ide=True - передача в IDE для отладки
Display.init(Display.ST7701, to_ide=True)
MediaManager.init()  # Инициализация менеджера медиа-ресурсов

sensor.run()  # Запуск потока данных с камеры
clock = time.clock()  # Таймер для подсчёта FPS (кадров в секунду)

# Основной бесконечный цикл обработки кадров
while True:
    clock.tick()  # Начало отсчёта времени для текущего кадра
    img = sensor.snapshot()  # Получение текущего кадра с камеры

    # Поиск линии методом линейной регрессии
    # get_regression() находит область с тёмными пикселями и строит линию
    line = img.get_regression([THRESHOLD])

    if line:
        # Если линия найдена - рисуем её на изображении
        # color=127 - серый цвет, thickness=4 - толщина 4 пикселя
        img.draw_line(line.line(), color=127, thickness=4)

        # === ВЫЧИСЛЕНИЕ ЦЕНТРА ЛИНИИ ===
        # x1 и x2 - это координаты X начала и конца найденной линии
        # Центр линии = среднее арифметическое между x1 и x2
        x_center = (line.x1() + line.x2()) / 2

        # === ВЫЧИСЛЕНИЕ ОШИБКИ (ERROR) ===
        # Центр экрана = IMG_WIDTH / 2 = 640 / 2 = 320 пикселей
        # error = положение центра линии - положение центра экрана
        # 
        # Примеры:
        # - если линия по центру: x_center = 320, error = 0
        # - если линия справа: x_center = 400, error = 80 (положительная)
        # - если линия слева: x_center = 240, error = -80 (отрицательная)
        error = x_center - (IMG_WIDTH / 2)

        # Отображаем значение ошибки на экране
        # %.1f означает: число с плавающей точкой, 1 знак после запятой
        img.draw_string(5, 5, "err: %.1f" % error, color=255)
        
        # Вывод ошибки в консоль для отладки
        print("ERROR:", error)
        
        # === ДЛЯ РОБОТА (пример, не реализовано в этом скрипте) ===
        # Здесь можно добавить управление моторами:
        # if error > 10:      # Линия справа - поворачиваем вправо
        #     turn_right()
        # elif error < -10:   # Линия слева - поворачиваем влево
        #     turn_left()
        # else:               # Линия по центру - едем прямо
        #     go_forward()

    # Вывод изображения на экран
    Display.show_image(img)
    
    # Вывод производительности (кадры в секунду)
    print("FPS:", clock.fps())
