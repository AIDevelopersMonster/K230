# ============================================================
# 02. Multi Color Recognition (несколько цветов)
#
# Что делает этот скрипт:
# - Получает изображение с камеры K230
# - Ищет объекты нескольких цветов одновременно
# - Выделяет каждый найденный объект прямоугольной рамкой
# - Отображает FPS (кадры в секунду)
#
# Как это работает:
# 1. Скрипт создаёт список LAB-порогов для разных цветов
# 2. В цикле проходит по каждому порогу цвета
# 3. Для каждого цвета ищет объекты на изображении
# 4. Рисует красную рамку вокруг всех найденных объектов
#
# Как пользоваться:
# 1. Откройте файл в CanMV IDE
# 2. Подключите плату K230 к компьютеру через USB
# 3. Наведите камеру на объекты разных цветов
# 4. Запустите скрипт кнопкой Run в CanMV IDE
#
# Настройка цветов:
# - Измените значения в списке THRESHOLDS для ваших цветов
# - Используйте Threshold Editor для подбора LAB-значений
#   (Tools → Machine Vision → Threshold Editor в CanMV IDE)
#
# Структура LAB-порога: (L Min, L Max, A Min, A Max, B Min, B Max)
# L - светлота (от 0 до 100)
# A - зелёно-красная компонента (от -128 до 127)
# B - сине-жёлтая компонента (от -128 до 127)
# ============================================================

# Импорт необходимых библиотек
import time  # Библиотека для работы со временем (нужна для подсчёта FPS)
from media.sensor import *  # Библиотека для управления камерой
from media.display import *  # Библиотека для управления дисплеем
from media.media import *  # Библиотека для управления медиа-ресурсами

# Настройка порогов LAB для разных цветов
# Формат: (L Min, L Max, A Min, A Max, B Min, B Max)
# Эти значения определяют диапазон цветов, которые будет искать скрипт
THRESHOLDS = [
    (0, 66, 7, 127, 3, 127),          # Красный цвет
    (42, 100, -128, -17, 6, 66),      # Зелёный цвет
    (43, 99, -43, -4, -56, -7),       # Синий цвет
]

# Инициализация камеры
sensor = Sensor()  # Создаём объект камеры
sensor.reset()  # Сбрасываем настройки камеры к заводским
sensor.set_framesize(width=640, height=480)  # Устанавливаем разрешение изображения 640x480 пикселей
sensor.set_pixformat(Sensor.RGB565)  # Устанавливаем формат пикселей RGB565 (16 бит на пиксель)

# Инициализация дисплея
Display.init(Display.ST7701, to_ide=True)  # Инициализируем дисплей ST7701 для вывода в IDE
MediaManager.init()  # Инициализируем менеджер медиа-ресурсов

# Запуск камеры
sensor.run()  # Запускаем захват изображений с камеры

# Создаём объект для подсчёта FPS (кадров в секунду)
clock = time.clock()

# Основной цикл программы (работает бесконечно)
while True:
    clock.tick()  # Начинаем отсчёт времени для текущего кадра
    img = sensor.snapshot()  # Делаем снимок с камеры и сохраняем в переменную img

    # Проходим по всем порогам цветов (красный, зелёный, синий)
    for threshold in THRESHOLDS:
        # Ищем объекты заданного цвета на изображении
        # find_blobs возвращает список найденных объектов (блобов)
        blobs = img.find_blobs([threshold])
        
        # Для каждого найденного объекта рисуем прямоугольную рамку
        for blob in blobs:
            # blob[0:4] содержит координаты прямоугольника (x, y, ширина, высота)
            # color=(255,0,0) - красный цвет рамки
            # thickness=3 - толщина линии рамки в пикселях
            img.draw_rectangle(blob[0:4], color=(255, 0, 0), thickness=3)

    # Показываем изображение на дисплее
    Display.show_image(img)
    
    # Выводим FPS в консоль для отладки
    print("FPS:", clock.fps())
