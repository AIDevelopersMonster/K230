# ============================================================
# 01. Color Recognition: базовое распознавание цвета с камеры
#
# Что делает этот скрипт:
# - Получает изображение с камеры K230
# - Ищет объекты заданного цвета с помощью LAB-фильтрации
# - Рисует прямоугольную рамку вокруг найденного объекта
# - Ставит крестик в центре объекта
# - Отображает название цвета и FPS на экране
#
# Как пользоваться:
# 1. Откройте файл в CanMV IDE
# 2. Подключите плату K230 к компьютеру через USB
# 3. Выберите нужный цвет, изменив переменную COLOR_INDEX
# 4. Наведите камеру на объект нужного цвета
# 5. Запустите скрипт кнопкой Run в CanMV IDE
#
# Доступные цвета (COLOR_INDEX):
# 0 - красный (RED)
# 1 - зелёный (GREEN)
# 2 - синий (BLUE)
# 3 - голубой/цвет логотипа YAHBOOM (YAHBOOM)
#
# Если цвет определяется плохо:
# - Улучшите освещение объекта
# - Уберите блики и отражения
# - Настройте LAB-пороги в Threshold Editor (Tools → Machine Vision → Threshold Editor)
# - Измените значения в списке THRESHOLDS под ваше освещение
# ============================================================

# Импорт необходимых библиотек
import time  # Библиотека для работы со временем (нужна для подсчёта FPS)
import os    # Библиотека для работы с операционной системой
import sys   # Библиотека для работы с системными параметрами
import gc    # Библиотека для управления сборщиком мусора (очистка памяти)
from media.sensor import *  # Библиотека для управления камерой
from media.display import *  # Библиотека для управления дисплеем
from media.media import *  # Библиотека для управления медиа-ресурсами
import image  # Библиотека для обработки изображений

# Настройки разрешения дисплея (ширина и высота в пикселях)
DISPLAY_WIDTH = 640   # Ширина изображения 640 пикселей
DISPLAY_HEIGHT = 480  # Высота изображения 480 пикселей

# Пороги LAB для разных цветов
# Формат: (L Min, L Max, A Min, A Max, B Min, B Max)
# L - светлота (от 0 до 100): 0=чёрный, 100=белый
# A - зелёно-красная компонента (от -128 до 127): отрицательные=зелёный, положительные=красный
# B - сине-жёлтая компонента (от -128 до 127): отрицательные=синий, положительные=жёлтый
THRESHOLDS = [
    (0, 66, 7, 127, 3, 127),          # 0: Красный цвет
    (42, 100, -128, -17, 6, 66),      # 1: Зелёный цвет
    (43, 99, -43, -4, -56, -7),       # 2: Синий цвет
    (37, 100, -128, 127, -128, -27),  # 3: Голубой/YAHBOOM
]

# Названия цветов для отображения на экране
COLOR_NAMES = ["RED", "GREEN", "BLUE", "YAHBOOM"]

# Индекс текущего цвета (0-3)
# Измените это число, чтобы выбрать другой цвет для распознавания
COLOR_INDEX = 0


def get_closest_rgb(lab_threshold):
    """
    Преобразует LAB-цвет в RGB для отрисовки.
    
    Аргументы:
        lab_threshold: кортеж из 6 значений (L min, L max, A min, A max, B min, B max)
    
    Возвращает:
        RGB значение среднего цвета из диапазона
    """
    # Вычисляем средние значения для каждой компоненты LAB
    l_center = (lab_threshold[0] + lab_threshold[1]) // 2  # Средняя светлота
    a_center = (lab_threshold[2] + lab_threshold[3]) // 2  # Средняя A-компонента
    b_center = (lab_threshold[4] + lab_threshold[5]) // 2  # Средняя B-компонента
    
    # Преобразуем LAB в RGB для получения цвета рамки
    return image.lab_to_rgb((l_center, a_center, b_center))


def init_sensor():
    """
    Инициализирует камеру с нужными настройками.
    
    Возвращает:
        Объект sensor для работы с камерой
    """
    sensor = Sensor()  # Создаём объект камеры
    sensor.reset()  # Сбрасываем настройки камеры к заводским
    # Устанавливаем разрешение изображения
    sensor.set_framesize(width=DISPLAY_WIDTH, height=DISPLAY_HEIGHT)
    # Устанавливаем формат пикселей RGB565 (16 бит на пиксель)
    sensor.set_pixformat(Sensor.RGB565)
    return sensor


def main():
    """
    Основная функция программы.
    Запускает камеру, обрабатывает изображения и отображает результат.
    """
    sensor = None  # Переменная для хранения объекта камеры
    
    try:
        # Инициализация оборудования
        sensor = init_sensor()  # Настраиваем камеру
        Display.init(Display.ST7701, to_ide=True)  # Инициализируем дисплей для вывода в IDE
        MediaManager.init()  # Инициализируем менеджер медиа-ресурсов
        sensor.run()  # Запускаем захват изображений с камеры
        
        clock = time.clock()  # Создаём таймер для подсчёта FPS
        
        # Получаем порог для выбранного цвета
        threshold = THRESHOLDS[COLOR_INDEX]
        # Получаем RGB цвет для отрисовки рамки
        draw_color = get_closest_rgb(threshold)
        
        # Основной цикл программы (работает бесконечно)
        while True:
            clock.tick()  # Начинаем отсчёт времени для текущего кадра
            img = sensor.snapshot()  # Делаем снимок с камеры
            
            # Ищем объекты заданного цвета на изображении
            # pixels_threshold=50 - минимальное количество пикселей для объекта
            # area_threshold=50 - минимальная площадь объекта
            # merge=True - объединять близкие объекты в один
            blobs = img.find_blobs([threshold], pixels_threshold=50, area_threshold=50, merge=True)
            
            # Обрабатываем каждый найденный объект
            for blob in blobs:
                # Рисуем прямоугольную рамку вокруг объекта
                # blob[0:4] содержит (x, y, ширина, высота)
                img.draw_rectangle(blob[0:4], color=draw_color, thickness=4)
                
                # Рисуем крестик в центре объекта
                # blob[5] - координата X центра, blob[6] - координата Y центра
                img.draw_cross(blob[5], blob[6], color=draw_color, thickness=2)
                
                # Рисуем название цвета над объектом
                img.draw_string(blob[0], max(0, blob[1] - 20), COLOR_NAMES[COLOR_INDEX], color=draw_color, scale=2)
            
            # Получаем количество кадров в секунду
            fps = clock.fps()
            
            # Рисуем FPS в левом верхнем углу
            img.draw_string_advanced(0, 0, 30, "FPS: %.2f" % fps, color=(255, 255, 255))
            
            # Показываем изображение на дисплее
            Display.show_image(img)
            
            # Выводим информацию в консоль для отладки
            print("FPS:", fps, "blobs:", len(blobs))
    
    except KeyboardInterrupt as e:
        # Обработка прерывания от пользователя (Ctrl+C)
        print("Пользователь остановил скрипт:", e)
    except Exception as e:
        # Обработка любых других ошибок
        print("Ошибка:", e)
    finally:
        # Освобождение ресурсов при завершении работы
        if sensor:
            sensor.stop()  # Останавливаем камеру
        Display.deinit()  # Деинициализируем дисплей
        MediaManager.deinit()  # Деинициализируем менеджер медиа
        gc.collect()  # Принудительно запускаем сборщик мусора для очистки памяти


if __name__ == "__main__":
    # Точка входа в программу
    # Запускаем основную функцию только если файл запущен напрямую
    main()
