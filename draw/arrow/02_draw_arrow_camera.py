# Описание:
# Демонстрация рисования стрелок поверх изображения с камеры.
# Скрипт захватывает изображение с камеры и рисует на нём цветные
# стрелки и текст для показа возможностей наложения графики.
#
# Используется:
# - Sensor (для работы с камерой)
# - Display (для вывода на экран)
# - MediaManager (для управления медиа-ресурсами)
#
# ============================================

# Импортируем необходимые библиотеки
import uos as os  # Библиотека операционной системы (для exitpoint)
from media.sensor import *  # Модуль для работы с сенсорами (камерой)
from media.display import *  # Модуль для управления дисплеем
from media.media import *  # Модуль для управления медиа-ресурсами

# Задаём размеры изображения (ширина и высота в пикселях)
WIDTH = 640
HEIGHT = 480

# Создаём объект сенсора (камеры)
sensor = Sensor()

# Сбрасываем настройки сенсора к значениям по умолчанию
sensor.reset()

# Устанавливаем размер кадра: ширина x высота для канала CAM_CHN_ID_1
# Это определяет разрешение изображения, которое будет снимать камера
sensor.set_framesize(width=WIDTH, height=HEIGHT, chn=CAM_CHN_ID_1)

# Устанавливаем формат пикселей RGB565 для канала CAM_CHN_ID_1
# RGB565 — это 16-битный формат цвета (5 бит красный, 6 бит зелёный, 5 бит синий)
sensor.set_pixformat(Sensor.RGB565, chn=CAM_CHN_ID_1)

# Инициализируем дисплей типа ST7701 с нашими размерами
# to_ide=True позволяет отображать изображение в IDE для отладки
Display.init(Display.ST7701, width=WIDTH, height=HEIGHT, to_ide=True)

# Инициализируем медиа-менеджер для работы с медиа-ресурсами
MediaManager.init()

# Запускаем сенсор (камеру) для начала захвата изображений
sensor.run()

# Основной бесконечный цикл программы
while True:
    # Проверяем точку выхода (позволяет корректно завершить программу)
    os.exitpoint()
    
    # Делаем снимок с камеры и получаем изображение
    # chn=CAM_CHN_ID_1 указывает, с какого канала брать изображение
    img = sensor.snapshot(chn=CAM_CHN_ID_1)

    # ============================================
    # Рисуем стрелки поверх изображения с камеры
    # Формат: img.draw_arrow(x0, y0, x1, y1, color=(R,G,B), thickness=толщина)
    # где (x0, y0) — начало стрелки, (x1, y1) — конец стрелки
    # ============================================
    
    # Первая стрелка: красная, идёт из точки (100,100) в точку (300,200)
    # color=(255,0,0) — красный цвет в формате RGB
    img.draw_arrow(100, 100, 300, 200, color=(255, 0, 0), thickness=2)
    
    # Вторая стрелка: зелёная, идёт из точки (300,200) в точку (500,300)
    # color=(0,255,0) — зелёный цвет в формате RGB
    img.draw_arrow(300, 200, 500, 300, color=(0, 255, 0), thickness=2)

    # Рисуем текст "Draw Arrow" в левом верхнем углу
    # Параметры: (x, y, текст, цвет)
    # color=(255,255,0) — жёлтый цвет в формате RGB
    img.draw_string(10, 10, "Draw Arrow", color=(255, 255, 0))

    # Отображаем изображение с нарисованными стрелками на дисплее
    Display.show_image(img)
