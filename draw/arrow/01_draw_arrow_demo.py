# Описание:
# Демонстрация рисования стрелок на экране с помощью функции draw_arrow().
# Скрипт создаёт изображение с белым фоном и рисует несколько стрелок
# разных цветов и толщины для показа возможностей API.
#
# Используется:
# - image (для создания изображения и рисования)
# - Display (для вывода на экран)
# - MediaManager (для управления медиа-ресурсами)
#
# ============================================

# Импортируем необходимые библиотеки
import time  # Библиотека для работы со временем (задержки)
import image  # Библиотека для работы с изображениями
from media.display import *  # Модуль для управления дисплеем
from media.media import *  # Модуль для управления медиа-ресурсами

# Задаём размеры экрана (ширина и высота в пикселях)
WIDTH = 640
HEIGHT = 480

# Создаём новое изображение с указанными размерами и форматом ARGB8888
# ARGB8888 означает, что каждый пиксель хранится как 4 байта: Alpha, Red, Green, Blue
img = image.Image(WIDTH, HEIGHT, image.ARGB8888)

# Очищаем изображение (делаем его полностью прозрачным/чёрным)
img.clear()

# Рисуем белый прямоугольник на всём изображении, чтобы создать белый фон
# Параметры: (x, y, ширина, высота, цвет, заполнение)
# color=(255,255,255) — белый цвет в формате RGB
# fill=True — заполнить прямоугольник цветом
img.draw_rectangle(0, 0, WIDTH, HEIGHT, color=(255, 255, 255), fill=True)

# Инициализируем дисплей типа ST7701 с нашими размерами
# to_ide=True позволяет отображать изображение в IDE для отладки
Display.init(Display.ST7701, width=WIDTH, height=HEIGHT, to_ide=True)

# Инициализируем медиа-менеджер для работы с медиа-ресурсами
MediaManager.init()

try:
    # ============================================
    # Рисуем стрелки на изображении
    # Формат: img.draw_arrow(x0, y0, x1, y1, color=(R,G,B), thickness=толщина)
    # где (x0, y0) — начало стрелки, (x1, y1) — конец стрелки
    # ============================================
    
    # Главная стрелка (горизонтальная, толстая, светло-голубая)
    # Начинается в точке (320, 200), заканчивается в (400, 200)
    img.draw_arrow(320, 200, 400, 200, color=(0, 191, 255), thickness=5)

    # Дополнительные стрелки (тонкие, небесно-голубые)
    # Первая дополнительная стрелка выше главной
    img.draw_arrow(300, 180, 380, 180, color=(135, 206, 235), thickness=3)
    # Вторая дополнительная стрелка ниже главной
    img.draw_arrow(340, 220, 420, 220, color=(135, 206, 235), thickness=3)

    # Диагональные стрелки (светло-голубые)
    # Стрелка идёт из верхнего левого угла в нижний правый
    img.draw_arrow(250, 150, 350, 250, color=(0, 191, 255), thickness=3)
    # Стрелка идёт из верхнего правого угла в нижний левый
    img.draw_arrow(350, 150, 450, 250, color=(0, 191, 255), thickness=3)

    # Отображаем готовое изображение на дисплее
    Display.show_image(img)

    # Бесконечный цикл для удержания изображения на экране
    # Программа будет ждать здесь, пока вы не остановите её вручную
    while True:
        time.sleep(2)  # Делаем паузу 2 секунды в каждом цикле

except Exception as e:
    # Если произошла ошибка, выводим её в консоль
    print(e)

finally:
    # Освобождаем ресурсы после завершения работы
    # Этот блок выполнится всегда, даже если была ошибка
    Display.deinit()  # Деинициализируем дисплей
    MediaManager.deinit()  # Деинициализируем медиа-менеджер
