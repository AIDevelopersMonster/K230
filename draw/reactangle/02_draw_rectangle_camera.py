# Описание:
# Рисование прямоугольников поверх изображения с камеры.
# Скрипт захватывает изображение с камеры и рисует на нём
# цветные прямоугольники (контур и заливку) как оверлей.
#
# Используется:
# - Sensor / Display / MediaManager
#
# ============================================

import uos as os
from media.sensor import *
from media.display import *
from media.media import *

# Устанавливаем разрешение экрана (ширина и высота в пикселях)
WIDTH = 640
HEIGHT = 480

# Создаём объект сенсора (камеры)
sensor = Sensor()

# Сбрасываем настройки сенсора к значениям по умолчанию
sensor.reset()

# Устанавливаем размер кадра (разрешение изображения)
# chn=CAM_CHN_ID_1 означает использование первого канала камеры
sensor.set_framesize(width=WIDTH, height=HEIGHT, chn=CAM_CHN_ID_1)

# Устанавливаем формат пикселей RGB565
# RGB565: 5 бит красный, 6 бит зелёный, 5 бит синий (всего 16 бит на пиксель)
sensor.set_pixformat(Sensor.RGB565, chn=CAM_CHN_ID_1)

# Инициализируем дисплей (ST7701 - контроллер экрана)
# to_ide=True позволяет видеть изображение в IDE
Display.init(Display.ST7701, width=WIDTH, height=HEIGHT, to_ide=True)

# Инициализируем медиа-менеджер для работы с изображениями
MediaManager.init()

# Запускаем сенсор (камеру) - начинаем захват изображения
sensor.run()

# Основной бесконечный цикл программы
while True:
    # Проверяем точку выхода (для корректного завершения программы)
    os.exitpoint()
    
    # Делаем снимок с камеры и получаем изображение
    # snapshot() возвращает объект изображения с текущим кадром
    img = sensor.snapshot(chn=CAM_CHN_ID_1)

    # ============================================
    # Рисуем прямоугольники поверх изображения с камеры
    # ============================================
    
    # Первый прямоугольник: красный контур
    # Параметры: (x, y, ширина, высота, цвет, толщина линии)
    # color=(255,0,0) - красный цвет (R=255, G=0, B=0)
    img.draw_rectangle(100, 100, 200, 150, color=(255,0,0), thickness=2)
    
    # Второй прямоугольник: зелёная заливка
    # fill=True означает, что прямоугольник будет полностью залит цветом
    # color=(0,255,0) - зелёный цвет (R=0, G=255, B=0)
    img.draw_rectangle(150, 150, 100, 80, color=(0,255,0), fill=True)

    # Добавляем текст на изображение
    # draw_string(x, y, текст, цвет)
    # color=(255,255,0) - жёлтый цвет (R=255, G=255, B=0)
    img.draw_string(10, 10, "Draw Rectangle", color=(255,255,0))

    # Показываем изображение с нарисованными прямоугольниками на экране
    Display.show_image(img)
