# Описание:
# Демонстрация рисования прямоугольников на экране K230.
# Скрипт создаёт изображение с несколькими прямоугольниками
# разных цветов и размеров, используя контур и заливку.
#
# Используется:
# - image.Image / Display / MediaManager
#
# ============================================

import time, os
import image
from media.display import *
from media.media import *

# Устанавливаем разрешение экрана (ширина и высота в пикселях)
WIDTH = 640
HEIGHT = 480

# Создаём пустое изображение нужного размера в формате ARGB8888
# ARGB8888 означает: 8 бит на канал (Alpha, Red, Green, Blue)
img = image.Image(WIDTH, HEIGHT, image.ARGB8888)

# Очищаем изображение (делаем его полностью прозрачным/чёрным)
img.clear()

# Рисуем белый фон - прямоугольник на весь экран с заливкой
# fill=True означает, что прямоугольник будет залит цветом
img.draw_rectangle(0, 0, WIDTH, HEIGHT, color=(255,255,255), fill=True)

# Инициализируем дисплей (ST7701 - контроллер экрана)
# to_ide=True позволяет видеть изображение в IDE
Display.init(Display.ST7701, width=WIDTH, height=HEIGHT, to_ide=True)

# Инициализируем медиа-менеджер для работы с изображениями
MediaManager.init()

try:
    # ============================================
    # Рисуем основной большой прямоугольник (контур)
    # Параметры: (x, y, ширина, высота, цвет, толщина)
    # Цвет (0,191,255) - голубой (R=0, G=191, B=255)
    # thickness=2 означает толщину линии 2 пикселя
    # ============================================
    img.draw_rectangle(120, 160, 400, 160, color=(0,191,255), thickness=2)

    # ============================================
    # Рисуем боковые прямоугольники с заливкой
    # ============================================
    
    # Левый верхний угол - светло-голубой (135,206,235)
    img.draw_rectangle(120, 160, 50, 50, color=(135,206,235), fill=True)
    
    # Левый нижний угол - голубой (0,191,255)
    img.draw_rectangle(120, 270, 50, 50, color=(0,191,255), fill=True)

    # Правый верхний угол - голубой (0,191,255)
    img.draw_rectangle(470, 160, 50, 50, color=(0,191,255), fill=True)
    
    # Правый нижний угол - светло-голубой (135,206,235)
    img.draw_rectangle(470, 270, 50, 50, color=(135,206,235), fill=True)

    # ============================================
    # Рисуем центральные блоки
    # ============================================
    
    # Внешний контур центрального блока
    img.draw_rectangle(220, 200, 200, 80, color=(0,191,255), thickness=2)
    
    # Внутренняя заливка центрального блока
    img.draw_rectangle(240, 220, 160, 40, color=(135,206,235), fill=True)

    # Показываем готовое изображение на экране
    Display.show_image(img)

    # Бесконечный цикл - держим изображение на экране
    while True:
        time.sleep(2)

except Exception as e:
    # Если произошла ошибка, выводим её в консоль
    print(e)

finally:
    # Освобождаем ресурсы - всегда выполняется в конце
    # deinit() означает "деинициализация" (отключение)
    Display.deinit()
    MediaManager.deinit()
