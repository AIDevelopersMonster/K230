# Описание:
# Демонстрация рисования случайных эллипсов на экране
# Скрипт создаёт изображение и рисует 10 эллипсов со случайными
# параметрами: позиция, размер, угол поворота и цвет
#
# Используется:
# - image.Image - создание и работа с изображением
# - media.display - вывод изображения на экран
# - media.media - управление медиа-системой
#
# ============================================

# Импорт необходимых библиотек
import time, os, math          # Время, операционная система, математика
import image                    # Библиотека для работы с изображениями
from random import randint      # Функция для генерации случайных чисел
from media.display import *     # Управление дисплеем
from media.media import *       # Управление медиа-системой

# Размеры экрана (ширина и высота в пикселях)
WIDTH = 640
HEIGHT = 480

# Создаём объект изображения с указанными размерами и форматом цвета ARGB8888
# ARGB8888 - формат цвета с альфа-каналом (прозрачностью), 8 бит на каждый канал
img = image.Image(WIDTH, HEIGHT, image.ARGB8888)

# Очищаем изображение (делаем его полностью прозрачным/чёрным)
img.clear()

# Рисуем белый прямоугольник на весь экран - это будет фон
# Параметры: (x, y, ширина, высота, цвет, заполнение)
# цвет=(255,255,255) - белый цвет в формате RGB
# fill=True - закрасить прямоугольник целиком
img.draw_rectangle(0, 0, WIDTH, HEIGHT, color=(255,255,255), fill=True)

# Инициализируем дисплей
# Display.ST7701 - тип контроллера дисплея
# width и height - размеры экрана
# to_ide=True - разрешение на вывод в IDE
Display.init(Display.ST7701, width=WIDTH, height=HEIGHT, to_ide=True)

# Инициализируем медиа-менеджер для работы с медиа-ресурсами
MediaManager.init()

try:
    # Основной цикл - рисуем 10 случайных эллипсов
    for i in range(10):
        # Генерируем случайные параметры для каждого эллипса
        
        # x, y - координаты центра эллипса (случайно от 0 до ширины/высоты экрана)
        x = randint(0, WIDTH)
        y = randint(0, HEIGHT)
        
        # rx, ry - радиусы эллипса по оси X и Y (размер эллипса)
        rx = randint(10, 200)   # Радиус по горизонтали от 10 до 200 пикселей
        ry = randint(10, 150)   # Радиус по вертикали от 10 до 150 пикселей
        
        # rot - угол поворота эллипса в градусах (от 0 до 360)
        rot = randint(0, 360)
        
        # color - случайный яркий цвет в формате RGB
        # Каждый компонент (R, G, B) принимает значение от 128 до 255
        # Это гарантирует, что цвета будут достаточно яркими
        color = (randint(128,255), randint(128,255), randint(128,255))

        # Рисуем эллипс на изображении
        # Параметры: центр_x, центр_y, радиус_x, радиус_y, угол_поворота, цвет, толщина_линии
        img.draw_ellipse(x, y, rx, ry, rot, color=color, thickness=2)

    # Выводим готовое изображение с эллипсами на дисплей
    Display.show_image(img)

    # Бесконечный цикл для удержания изображения на экране
    # Пауза 2 секунды между проверками
    while True:
        time.sleep(2)

except Exception as e:
    # Обработка любых ошибок - выводим сообщение об ошибке
    print(e)

finally:
    # Блок finally выполняется всегда, даже если произошла ошибка
    # Освобождаем ресурсы - отключаем дисплей и медиа-менеджер
    Display.deinit()
    MediaManager.deinit()
