# Описание:
# Рисование эллипсов поверх изображения с камеры
# Скрипт захватывает изображение с камеры и рисует на нём
# два эллипса (красный и зелёный) в центре экрана
#
# Используется:
# - media.sensor - управление камерой
# - media.display - вывод изображения на экран
# - media.media - управление медиа-системой
#
# ============================================

# Импорт необходимых библиотек
import uos as os                # Операционная система (псевдоним 'os')
from media.sensor import *      # Управление сенсором/камерой
from media.display import *     # Управление дисплеем
from media.media import *       # Управление медиа-системой

# Размеры экрана (ширина и высота в пикселях)
WIDTH = 640
HEIGHT = 480

# Создаём объект сенсора (камеры)
sensor = Sensor()

# Сбрасываем настройки сенсора к значениям по умолчанию
sensor.reset()

# Устанавливаем размер кадра (разрешение изображения)
# width=WIDTH, height=HEIGHT - размеры 640x480 пикселей
# chn=CAM_CHN_ID_1 - используем первый канал камеры
sensor.set_framesize(width=WIDTH, height=HEIGHT, chn=CAM_CHN_ID_1)

# Устанавливаем формат пикселей RGB565
# RGB565 - 16-битный формат цвета (5 бит красный, 6 бит зелёный, 5 бит синий)
# chn=CAM_CHN_ID_1 - применяем к первому каналу камеры
sensor.set_pixformat(Sensor.RGB565, chn=CAM_CHN_ID_1)

# Инициализируем дисплей
# Display.ST7701 - тип контроллера дисплея
# width и height - размеры экрана
# to_ide=True - разрешение на вывод в IDE
Display.init(Display.ST7701, width=WIDTH, height=HEIGHT, to_ide=True)

# Инициализируем медиа-менеджер для работы с медиа-ресурсами
MediaManager.init()

# Запускаем сенсор (камеру) - начинаем захват изображения
sensor.run()

# Основной бесконечный цикл программы
while True:
    # Проверяем точку выхода (например, нажатие кнопки остановки в IDE)
    # Это позволяет корректно завершить программу извне
    os.exitpoint()
    
    # Делаем снимок с камеры и получаем объект изображения
    # chn=CAM_CHN_ID_1 - снимаем с первого канала камеры
    img = sensor.snapshot(chn=CAM_CHN_ID_1)

    # Рисуем первый эллипс в центре экрана
    # Параметры: центр_x, центр_y, радиус_x, радиус_y, угол_поворота, цвет, толщина
    # WIDTH//2, HEIGHT//2 - центр экрана (делим нацело на 2)
    # 120, 60 - большой эллипс (радиус 120 по горизонтали, 60 по вертикали)
    # 0 - без поворота
    # color=(255,0,0) - красный цвет в формате RGB
    # thickness=2 - толщина линии 2 пикселя
    img.draw_ellipse(WIDTH//2, HEIGHT//2, 120, 60, 0, color=(255,0,0), thickness=2)
    
    # Рисуем второй эллипс (поменьше) в центре экрана
    # 60, 30 - меньший эллипс (радиус 60 по горизонтали, 30 по вертикали)
    # color=(0,255,0) - зелёный цвет в формате RGB
    img.draw_ellipse(WIDTH//2, HEIGHT//2, 60, 30, 0, color=(0,255,0), thickness=2)

    # Добавляем текст в левый верхний угол
    # Параметры: x, y, текст, цвет
    # color=(255,255,0) - жёлтый цвет (красный + зелёный)
    img.draw_string(10, 10, "Draw Ellipse", color=(255,255,0))

    # Выводим итоговое изображение (камера + эллипсы + текст) на дисплей
    Display.show_image(img)
