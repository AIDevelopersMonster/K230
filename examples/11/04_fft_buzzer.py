# Описание:
# Пример использования FFT для анализа сигнала и управления зуммером.
# Скрипт генерирует сигнал, выполняет частотный анализ и издаёт звук
# зуммером, если максимальная амплитуда частоты превышает порог.
#
# Используется:
# - YbBuzzer для управления зуммером
