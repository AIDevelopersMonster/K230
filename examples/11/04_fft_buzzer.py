# Пример 4: FFT анализ со звуковой индикацией через зуммер
# Этот скрипт показывает, как использовать результаты FFT для управления зуммером

# Импортируем необходимые модули
from machine import FFT                # Модуль для выполнения БПФ
from ybUtils.YbBuzzer import YbBuzzer  # Утилита для управления зуммером
from ulab import numpy as np           # Библиотека для работы с числовыми массивами
import math                            # Математические функции (cos, PI)

# Создаём объект для управления зуммером
buzzer = YbBuzzer()

# Параметры сигнала
N = 64  # Количество точек в сигнале (размер выборки)
PI = 3.1415926  # Число Пи для расчётов

# ============================================
# Основной цикл программы
# ============================================
while True:
    # ============================================
    # Шаг 1: Генерация тестового сигнала
    # ============================================
    data = []  # Список для хранения значений сигнала
    
    # Генерируем сигнал - косинусоида с амплитудой 100 и частотой 5 циклов на N точек
    for i in range(N):
        val = 100 * math.cos(5 * 2 * PI * i / N)
        data.append(int(val))

    # ============================================
    # Шаг 2: Подготовка данных и выполнение FFT
    # ============================================
    # Преобразуем список в массив numpy типа uint16
    arr = np.array(data, dtype=np.uint16)
    
    # Создаём объект FFT и запускаем анализ
    fft = FFT(arr, N, 0x555)
    res = fft.run()
    
    # Получаем амплитуды частотных компонент
    ampl = fft.amplitude(res)

    # ============================================
    # Шаг 3: Анализ результатов и управление зуммером
    # ============================================
    # Проверяем, превышает ли максимальная амплитуда порог 2000
    if max(ampl) > 2000:
        # Если порог превышен - издаём звуковой сигнал
        buzzer.beep()
