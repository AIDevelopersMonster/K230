# Пример 3: FFT анализ с управлением RGB светодиодом
# Этот скрипт показывает, как использовать результаты FFT для управления hardware

# Импортируем необходимые модули
from machine import FFT              # Модуль для выполнения БПФ
from ybUtils.YbRGB import YbRGB      # Утилита для управления RGB светодиодом
from ulab import numpy as np         # Библиотека для работы с числовыми массивами
import math                          # Математические функции (cos, PI)

# Создаём объект для управления RGB светодиодом
rgb = YbRGB()

# Параметры сигнала
N = 64  # Количество точек в сигнале (размер выборки)
PI = 3.1415926  # Число Пи для расчётов

# ============================================
# Основной цикл программы
# ============================================
while True:
    # ============================================
    # Шаг 1: Генерация тестового сигнала
    # ============================================
    data = []  # Список для хранения значений сигнала
    
    # Генерируем сигнал - косинусоида с амплитудой 50
    for i in range(N):
        val = 50 * math.cos(2 * PI * i / N)
        data.append(int(val))

    # ============================================
    # Шаг 2: Подготовка данных и выполнение FFT
    # ============================================
    # Преобразуем список в массив numpy типа uint16
    arr = np.array(data, dtype=np.uint16)
    
    # Создаём объект FFT и запускаем анализ
    fft = FFT(arr, N, 0x555)
    res = fft.run()
    
    # Получаем амплитуды частотных компонент
    ampl = fft.amplitude(res)

    # ============================================
    # Шаг 3: Анализ результатов и управление LED
    # ============================================
    # Находим максимальную амплитуду среди всех частот
    peak = max(ampl)

    # Если максимальная амплитуда превышает порог 1000:
    if peak > 1000:
        # Включаем красный цвет (сигнал сильный)
        rgb.show_rgb((255, 0, 0))
    else:
        # Включаем синий цвет (сигнал слабый)
        rgb.show_rgb((0, 0, 255))
