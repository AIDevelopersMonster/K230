# Пример 1: Базовое использование FFT
# Этот скрипт показывает, как выполнить простейший частотный анализ сигнала

# Импортируем необходимые модули
from machine import FFT          # Модуль для выполнения БПФ
import math                      # Математические функции (cos, PI)
from ulab import numpy as np     # Библиотека для работы с числовыми массивами

PI = 3.1415926  # Число Пи для расчётов

# ============================================
# Шаг 1: Генерация тестового сигнала
# ============================================
# Создаём пустой список для хранения значений сигнала
data = []
N = 64  # Количество точек в сигнале (размер выборки)

# Генерируем сигнал как сумму двух косинусоид с разными частотами и амплитудами:
# - Первая волна: амплитуда 10, частота 1 цикл на N точек
# - Вторая волна: амплитуда 30, частота 3 цикла на N точек
for i in range(N):
    # Вычисляем значение сигнала в точке i
    val = 10 * math.cos(2 * PI * i / N) + 30 * math.cos(3 * 2 * PI * i / N)
    data.append(int(val))  # Добавляем целочисленное значение в список

# ============================================
# Шаг 2: Подготовка данных для FFT
# ============================================
# Преобразуем список в массив numpy типа uint16 (требуется для FFT)
arr = np.array(data, dtype=np.uint16)

# ============================================
# Шаг 3: Создание объекта FFT и запуск анализа
# ============================================
# Параметры FFT:
# - arr: массив с данными сигнала
# - N: размер выборки (должен быть степенью двойки: 64, 128, 256...)
# - 0x555: параметр окна (window parameter), влияет на точность анализа
fft = FFT(arr, N, 0x555)

# Запускаем вычисление БПФ
res = fft.run()

# Получаем амплитуды частотных компонент
ampl = fft.amplitude(res)

# ============================================
# Шаг 4: Вывод результатов
# ============================================
# Печатаем массив амплитуд для каждой частоты
print("Amplitude:", ampl)
