# Описание:
# Базовый пример использования FFT (Быстрого Преобразования Фурье).
# Скрипт генерирует тестовый сигнал из двух косинусоид и выполняет
# его частотный анализ, выводя амплитуды найденных частот.
#
# Используется:
# - FFT (встроенный модуль)
# - ulab.numpy для работы с массивами
#
