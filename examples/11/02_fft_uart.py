"""
❓ Главный вопрос, на который отвечает БПФ:
   👉 "Из каких частот состоит мой сигнал?"

┌─────────────────┬────────────────────────────┐
│ Тип сигнала     │ Спектр (результат FFT)     │
├─────────────────┼────────────────────────────┤
│ Постоянный (DC) │ Пик только в бине 0        │
│ Чистый синус    │ Один чёткий пик            │
│ Сложная форма   │ Несколько пиков/гармоник   │
│ Пила/Меандр     │ Набор гармоник (1,3,5...)  │
│ Сумма частот    │ Пик на каждую частоту      │
│ Белый шум       │ Равномерный "фон"          │
│ Реальный сигнал │ Комбинация вышеперечисленного │
└─────────────────┴────────────────────────────┘

📌 Ключевые соответствия:
   • index в массиве amplitude → номер частотного бина
   • amplitude[index] → "сила" (мощность) этой частоты
   • Частота в Гц = index × (Fs / N), где Fs — частота дискретизации

🔍 Как интерпретировать результат:
   1. Найти максимальное значение в amplitude[]
   2. Запомнить его индекс — это доминирующая частота
   3. Проверить соседние бины — возможна "утечка" спектра
   4. Учесть зеркальные частоты (индексы > N/2)

⚙️ Параметры, влияющие на точность:
   • N (размер FFT): больше → лучше разрешение по частоте
   • Fs (частота дискретизации): выше → шире диапазон частот
   • Окно (windowing): снижает утечку спектра, но "размазывает" пики
   • Пересечение кадров: улучшает временное разрешение

💡 Советы для практики:
   ✓ Всегда используйте степень двойки для размера FFT
   ✓ Перед FFT вычитайте среднее значение (убирайте DC)
   ✓ Для нестационарных сигналов используйте скользящее окно
   ✓ Проверяйте, не превышает ли сигнал диапазон АЦП
   ✓ Учитывайте задержку: анализ идёт по уже полученным данным
"""


# ============================================
# ⚙️ РЕАЛИЗАЦИЯ: КОД ДЛЯ YAHBOOM K230
# ============================================

from machine import FFT
from ybUtils.YbUart import YbUart
from ulab import numpy as np

# 🔧 Инициализация UART для приёма данных
# Скорость должна совпадать с отправителем (ПК, датчик, другой МК)
uart = YbUart(115200)

# 📦 Буфер для накопления сырых данных из UART
buffer = ""

# 🗃️ Массив для сбора 64 отсчётов перед обработкой
nums_total = []


def is_power_of_two(n):
    """
    Проверка: является ли число степенью двойки.
    Требуется для корректной работы алгоритма БПФ.
    """
    return n > 0 and (n & (n - 1)) == 0


# ♾️ Главный цикл обработки данных
while True:
    try:
        # 📥 Чтение сырых байт из UART (неблокирующее)
        data = uart.read()
    except Exception:
        # При ошибке чтения — пробуем снова
        continue

    if data:
        try:
            # 🔤 Декодирование байт в строку (UTF-8/ASCII)
            text = data.decode()

            # 🧹 Очистка от пробелов и служебных символов
            # Важно: убираем \r\n, чтобы не ломать парсинг
            text = text.replace(" ", "").replace("\r", "").replace("\n", "")

            # ➕ Добавляем новые данные в буфер
            buffer += text
            # 🖨️ Отладка: показываем текущий буфер
            print("buffer:", buffer)

        except Exception:
            # Если декодирование не удалось — пропускаем кадр
            continue

        # ✂️ Разделение строки по запятым на отдельные числа
        # Пример: "10,20,30," → ["10", "20", "30", ""]
        parts = buffer.split(",")

        # 🔁 Последний элемент может быть неполным — оставляем в буфере
        buffer = parts[-1]   # "хвост" для следующего цикла
        parts = parts[:-1]   # полные числа для обработки

        # 🔢 Преобразование строк в целые числа
        for p in parts:
            if p != "":  # Пропускаем пустые строки
                try:
                    nums_total.append(int(p))
                except ValueError:
                    # Если число некорректное — игнорируем
                    pass

        # 📊 Инфо о накопленных данных
        print("Collected:", len(nums_total))

        # 🚀 Запуск FFT, как только набралось 64 отсчёта
        if len(nums_total) >= 64:
            # ✂️ Берём первые 64, остальное оставляем на следующий раз
            nums = nums_total[:64]
            nums_total = nums_total[64:]

            # 🖨️ Отладочная информация
            print("\n--- FFT START ---")
            print("LEN:", len(nums))
            print("nums:", nums[:10])  # Первые 10 значений

            try:
                # ✅ Критически важно: правильный тип данных!
                # int16 — диапазон -32768..32767, экономия памяти
                arr = np.array(nums, dtype=np.int16)
                print("arr:", arr[:10])

                # ⚙️ Инициализация и запуск БПФ:
                # • arr — входной массив (64 элемента)
                # • 64 — размер преобразования
                # • 0x555 — флаг конфигурации (зависит от реализации)
                fft = FFT(arr, 64, 0x555)
                res = fft.run()  # Выполнение БПФ

                # 📐 Вычисление амплитудного спектра из комплексного результата
                ampl = fft.amplitude(res)

                # 🔍 Поиск доминирующей частоты
                max_val = max(ampl)           # Максимальная амплитуда
                max_index = ampl.index(max_val)  # Её позиция (индекс бина)

                # 🖨️ Вывод результата
                print("Peak index:", max_index)
                print("Amplitude:", max_val)
                
                # 💡 Дополнительно: расчёт частоты в Гц (если известна Fs)
                # Fs = 1000  # Пример: 1000 отсчётов/сек
                # freq_hz = max_index * (Fs / 64)
                # print("Frequency:", freq_hz, "Hz")

            except Exception as e:
                # 🚨 Обработка ошибок БПФ
                print("FFT error:", e)
                # Возможные причины:
                # • Неверный тип данных в массиве
                # • Размер не степень двойки
                # • Переполнение при вычислениях
