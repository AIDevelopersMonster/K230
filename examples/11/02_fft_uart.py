# Пример 2: FFT анализ данных, полученных через UART
# Этот скрипт показывает, как обрабатывать внешние данные, поступающие через последовательный порт

# Импортируем необходимые модули
from machine import FFT              # Модуль для выполнения БПФ
from ybUtils.YbUart import YbUart    # Утилита для работы с UART
from ulab import numpy as np         # Библиотека для работы с числовыми массивами

# Создаём объект UART со скоростью 115200 бод
uart = YbUart(115200)

# ============================================
# Основной цикл программы
# ============================================
while True:
    # Читаем данные из UART
    # Ожидаем данные в формате: "число1,число2,число3,..."
    data = uart.read()
    
    # Если данные получены
    if data:
        try:
            # Декодируем байты в строку и разбиваем по запятым
            # Преобразуем каждую часть в целое число
            nums = [int(x) for x in data.decode().split(',')]
            
            # Создаём массив numpy из полученных чисел (тип uint16 требуется для FFT)
            arr = np.array(nums, dtype=np.uint16)
            
            # Создаём объект FFT с параметрами:
            # - arr: массив с данными
            # - len(nums): размер выборки
            # - 0x555: параметр окна
            fft = FFT(arr, len(nums), 0x555)
            
            # Запускаем вычисление БПФ
            res = fft.run()
            
            # Получаем амплитуды частотных компонент
            ampl = fft.amplitude(res)
            
            # Выводим результаты анализа
            print("FFT:", ampl)
            
        except Exception as e:
            # Обрабатываем возможные ошибки (неверный формат данных и т.д.)
            print("Error:", e)
