# Пример: использование FPIOA для GPIO
#
# Здесь мы назначим пин как обычный GPIO и будем мигать светодиодом

# Импортируем необходимые модули:
# FPIOA - для назначения функций на физические пины
# Pin - для управления цифровым входом/выходом (GPIO)
# time - для создания задержек между включениями/выключениями
from machine import FPIOA, Pin
import time

# Создаём объект FPIOA для работы с назначением функций пинов
fpioa = FPIOA()

# Назначаем физический пин 33 как GPIO33
# Это значит, что теперь он работает как обычный цифровой вывод
# Функция set_function принимает номер физического пина и функцию, которую нужно назначить
fpioa.set_function(33, FPIOA.GPIO33)

# Создаём объект GPIO (цифровой выход)
# Первый параметр - номер пина (33)
# Второй параметр - режим работы (Pin.OUT означает выход, т.е. мы можем подавать напряжение)
led = Pin(33, Pin.OUT)

print("Мигаем светодиодом...")

# Бесконечный цикл для постоянного мигания
while True:
    led.value(1)  # включить светодиод (подать высокое напряжение на пин)
    time.sleep(0.5)  # ждать 0.5 секунды (500 миллисекунд)
    led.value(0)  # выключить светодиод (подать низкое напряжение на пин)
    time.sleep(0.5)  # ждать 0.5 секунды перед следующим включением

# Важно:
# Если светодиод не мигает — проверь, что именно этот пин выведен на плате
# На разных платах светодиод может быть подключен к разным пинам