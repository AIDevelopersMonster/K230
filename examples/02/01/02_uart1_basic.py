# Пример: настройка UART через FPIOA
#
# Используем стандартные пины:
# GPIO9 -> TX (передача данных)
# GPIO10 -> RX (приём данных)

# Импортируем необходимые модули:
# FPIOA - для назначения функций на физические пины
# UART - для работы с последовательным интерфейсом связи
from machine import FPIOA, UART

# Создаём объект FPIOA для работы с назначением функций пинов
fpioa = FPIOA()

# Назначаем функции на пины:
# set_function(номер_пина, функция, ie=вход, oe=выход)
# Для TX (передача): ie=0 (вход отключен), oe=1 (выход включен)
# Для RX (приём): ie=1 (вход включен), oe=0 (выход отключен)
fpioa.set_function(9, FPIOA.UART1_TXD, ie=0, oe=1)
fpioa.set_function(10, FPIOA.UART1_RXD, ie=1, oe=0)

# Инициализируем UART интерфейс:
# Первый параметр - номер UART (UART1)
# Второй параметр - скорость передачи данных (115200 бит/секунду)
uart = UART(UART.UART1, 115200)

print("UART готов. Отправляем сообщение...")

# Отправляем строку через UART интерфейс
# write() передаёт данные в последовательный порт
uart.write("Hello from K230!\n")

