# Пример: настройка I2C через FPIOA
#
# Используем пины:
# GPIO32 -> SCL (тактовый сигнал)
# GPIO33 -> SDA (линия данных)

# Импортируем необходимые модули:
# FPIOA - для назначения функций на физические пины
# I2C - для работы с последовательной шиной I2C
from machine import FPIOA, I2C

# Создаём объект FPIOA для работы с назначением функций пинов
fpioa = FPIOA()

# Назначаем функции на пины:
# SCL (Serial Clock Line) - тактовый сигнал для синхронизации
# SDA (Serial Data Line) - линия для передачи данных
# Номер первого параметра - это номер физического пина на плате
fpioa.set_function(32, FPIOA.IIC0_SCL)
fpioa.set_function(33, FPIOA.IIC0_SDA)

# Инициализируем I2C интерфейс:
# Первый параметр - номер шины I2C (0)
# Второй параметр - частота в Герцах (100000 Гц = 100 кГц - стандартная скорость I2C)
i2c = I2C(0, freq=100000)

print("Сканируем I2C устройства...")

# Метод scan() автоматически опрашивает все возможные адреса на шине I2C
# и возвращает список адресов найденных устройств
devices = i2c.scan()
print("Найдено устройств:", devices)

