# Пример: базовая работа с FPIOA
#
# Этот файл показывает основные возможности FPIOA:
# - просмотр доступных функций
# - просмотр конфигурации пинов
# - поиск, где используется функция
#
# Подходит для самого первого знакомства

# Импортируем модуль FPIOA для работы с назначением функций пинов
# FPIOA (Field Programmable Input/Output Array) позволяет гибко настраивать
# какие функции будут выполнять физические пины микроконтроллера
from machine import FPIOA

# Создаём объект FPIOA для работы с назначением функций пинов
fpioa = FPIOA()

# Выводим заголовок раздела
print("=== Все доступные функции и пины ===")
# Метод help() без параметров показывает ВСЮ таблицу соответствий
# Какие функции можно назначить на какие пины
fpioa.help()  # Показывает ВСЁ, что умеет чип

print("\n=== Информация о пине 0 ===")
# Метод help() с номером пина показывает подробную информацию о конкретном пине
# Какие функции можно назначить на этот пин и текущую конфигурацию
fpioa.help(0)  # Подробная информация о конкретном пине

print("\n=== Где можно использовать IIC0_SDA ===")
# Метод help() с параметром func=True показывает на каких пинах
# можно использовать указанную функцию
fpioa.help(FPIOA.IIC0_SDA, func=True)

print("\n=== На каком пине сейчас UART0_TXD ===")
# Метод get_pin_num() возвращает номер физического пина,
# на котором сейчас назначена указанная функция
pin = fpioa.get_pin_num(FPIOA.UART0_TXD)
print("UART0_TXD находится на пине:", pin)

print("\n=== Какая функция у пина 0 ===")
# Метод get_pin_func() возвращает функцию, которая сейчас назначена на указанный пин
func = fpioa.get_pin_func(0)
print("Функция пина 0:", func)

# Совет:
# Перед тем как назначать функцию — всегда проверяй через help(),
# чтобы не попасть на неподдерживаемый пин.
# Не все функции можно назначить на любые пины!