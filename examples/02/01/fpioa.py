# Пример: базовая работа с FPIOA
#
# Этот файл показывает основные возможности FPIOA:
# - просмотр доступных функций
# - просмотр конфигурации пинов
# - поиск, где используется функция
#
# Подходит для самого первого знакомства

from machine import FPIOA

# Создаём объект FPIOA
fpioa = FPIOA()

print("=== Все доступные функции и пины ===")
fpioa.help()  # Показывает ВСЁ, что умеет чип

print("\n=== Информация о пине 0 ===")
fpioa.help(0)  # Подробная информация о конкретном пине

print("\n=== Где можно использовать IIC0_SDA ===")
fpioa.help(FPIOA.IIC0_SDA, func=True)

print("\n=== На каком пине сейчас UART0_TXD ===")
pin = fpioa.get_pin_num(FPIOA.UART0_TXD)
print("UART0_TXD находится на пине:", pin)

print("\n=== Какая функция у пина 0 ===")
func = fpioa.get_pin_func(0)
print("Функция пина 0:", func)

# Совет:
# Перед тем как назначать функцию — всегда проверяй через help(),
# чтобы не попасть на неподдерживаемый пин.