# Описание:
# Простая мелодия на PWM (звуковые сигналы)
# Изменяя частоту PWM сигнала, создаём разные ноты
# Частота звука определяет высоту тона
#
# Используется:
# - PWM (для генерации звуковых волн разной частоты)
# - FPIOA (для назначения функции на пин)
#
# ============================================

# Импортируем необходимые модули:
# PWM - класс для работы с широтно-импульсной модуляцией
# FPIOA - класс для настройки функций пинов
# time - модуль для работы со временем (задержки)
from machine import PWM, FPIOA
import time

# Создаём объект для управления пинами
fpioa = FPIOA()

# Настраиваем пин IO42 на работу в режиме PWM0
# К этому пину можно подключить динамик или пьезоизлучатель
fpioa.set_function(42, fpioa.PWM0)

# Создаём PWM объект на пине 42
# Начальные параметры: частота 1000 Гц, скважность 50%
pwm = PWM(42, freq=1000, duty=50)

# Список частот нот (в Герцах) для простой мелодии
# Чем выше частота, тем выше звук
# 500 Гц ≈ нота Si (B4)
# 800 Гц ≈ нота Sol# (G#5)
# 1000 Гц ≈ нота Si (B5)
# 1200 Гц ≈ нота Re# (D#6)
notes = [500, 800, 1000, 1200]

# Бесконечный цикл программы
while True:
    # Проходим по каждой ноте в списке
    for note in notes:
        # Устанавливаем частоту PWM равной частоте ноты
        # Это изменяет высоту издаваемого звука
        pwm.freq(note)
        
        # Ждём 0.3 секунды (300 миллисекунд)
        # Это длительность звучания каждой ноты
        time.sleep(0.3)
