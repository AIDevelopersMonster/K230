# Описание:
# Управление PWM через UART интерфейс
# Можно отправлять числа (0-100) через последовательный порт
# и изменять скважность PWM сигнала удалённо
#
# Используется:
# - PWM (для управления яркостью)
# - FPIOA (для назначения функции на пин)
# - YbUart (для приёма данных через UART)
#
# ============================================

# Импортируем необходимые модули:
# PWM - класс для работы с широтно-импульсной модуляцией
# FPIOA - класс для настройки функций пинов
# YbUart - утилита для работы с последовательным портом UART
# time - модуль для работы со временем (задержки)
from machine import PWM, FPIOA
from ybUtils.YbUart import YbUart
import time

# Создаём объект для управления пинами
fpioa = FPIOA()

# Настраиваем пин IO42 на работу в режиме PWM0
fpioa.set_function(42, fpioa.PWM0)

# Создаём PWM объект на пине 42
# Начальные параметры: частота 1000 Гц, скважность 50%
pwm = PWM(42, freq=1000, duty=50)

# Создаём объект для работы с UART
# baudrate=115200 - скорость передачи данных (бит в секунду)
# Это стандартная скорость для многих устройств
uart = YbUart(baudrate=115200)

# Бесконечный цикл программы
while True:
    # Читаем данные из UART порта
    # read() возвращает полученные байты или None, если данных нет
    data = uart.read()
    
    # Если данные получены (data не пустой)
    if data:
        try:
            # Преобразуем полученные байты в строку, удаляем пробелы
            # и конвертируем в целое число
            # Например, если отправили "50", то duty станет равно 50
            duty = int(data.decode().strip())
            
            # Устанавливаем новую скважность для PWM сигнала
            pwm.duty(duty)
            
            # Выводим сообщение об успешной установке значения
            print("Duty set:", duty)
        except:
            # Если произошла ошибка (например, отправили не число),
            # выводим сообщение об ошибке
            print("Error")
    
    # Небольшая задержка в цикле
    # Нужна, чтобы не перегружать процессор постоянными проверками
    time.sleep_ms(50)
