# Импортируем необходимые модули:
# PWM - класс для работы с широтно-импульсной модуляцией
# FPIOA - класс для настройки функций пинов (Function Pin Input/Output Array)
# time - модуль для работы со временем (задержки)
from machine import PWM, FPIOA
import time

# Создаём объект для управления пинами
fpioa = FPIOA()

# Настраиваем пин IO42 на работу в режиме PWM0
# FPIOA позволяет "назначить" функцию (например, PWM) на любой доступный пин
fpioa.set_function(42, fpioa.PWM0)

# Создаём PWM объект на пине 42
# Параметры:
# - freq=1000: частота сигнала 1000 Гц (1 кГц)
# - duty=50: скважность 50% (сигнал включён половину времени)
# Скважность (duty cycle) - это процент времени, когда сигнал находится в высоком состоянии
# 50% означает, что сигнал половину периода высокий, половину - низкий
pwm0 = PWM(42, freq=1000, duty=50)

# Бесконечный цикл программы
# В этом примере мы просто поддерживаем сигнал, ничего не меняя
while True:
    # Ждём 1 секунду перед следующей итерацией цикла
    # Это нужно, чтобы программа не нагружала процессор постоянными проверками
    time.sleep(1)
