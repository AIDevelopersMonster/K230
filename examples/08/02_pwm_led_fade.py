# Описание:
# Плавное изменение яркости светодиода с помощью PWM
# Демонстрирует эффект "затухания" (fade) - LED плавно загорается и гаснет
#
# Используется:
# - PWM (для управления яркостью)
# - FPIOA (для назначения функции на пин)
#
# ============================================

# Импортируем необходимые модули:
# PWM - класс для работы с широтно-импульсной модуляцией
# FPIOA - класс для настройки функций пинов
# time - модуль для работы со временем (задержки)
from machine import PWM, FPIOA
import time

# Создаём объект для управления пинами
fpioa = FPIOA()

# Настраиваем пин IO42 на работу в режиме PWM0
# Это позволит управлять яркостью подключенного светодиода
fpioa.set_function(42, fpioa.PWM0)

# Создаём PWM объект на пине 42
# Начальные параметры: частота 1000 Гц, скважность 50%
pwm = PWM(42, freq=1000, duty=50)

# Бесконечный цикл программы
while True:
    # Первый цикл: плавно увеличиваем яркость от 0% до 100%
    # range(0, 100, 5) означает: от 0 до 100 с шагом 5
    for duty in range(0, 100, 5):
        # Устанавливаем новую скважность (яркость)
        pwm.duty(duty)
        # Ждём 50 миллисекунд перед следующим изменением
        # Чем меньше задержка, тем быстрее будет изменение
        time.sleep_ms(50)
    
    # Второй цикл: плавно уменьшаем яркость от 100% до 0%
    # range(100, 0, -5) означает: от 100 до 0 с шагом -5 (в обратную сторону)
    for duty in range(100, 0, -5):
        # Устанавливаем новую скважность (яркость)
        pwm.duty(duty)
        # Ждём 50 миллисекунд
        time.sleep_ms(50)
