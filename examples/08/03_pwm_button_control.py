# Описание:
# Управление яркостью PWM с помощью кнопки
# При нажатии кнопки скважность увеличивается на 10%
# После достижения 100% сбрасывается в 0%
#
# Используется:
# - PWM (для управления яркостью)
# - FPIOA (для назначения функции на пин)
# - YbKey (для чтения состояния кнопки)
#
# ============================================

# Импортируем необходимые модули:
# PWM - класс для работы с широтно-импульсной модуляцией
# FPIOA - класс для настройки функций пинов
# YbKey - утилита для работы с кнопкой на плате Yahboom
# time - модуль для работы со временем (задержки)
from machine import PWM, FPIOA
from ybUtils.YbKey import YbKey
import time

# Создаём объект для управления пинами
fpioa = FPIOA()

# Настраиваем пин IO42 на работу в режиме PWM0
fpioa.set_function(42, fpioa.PWM0)

# Создаём PWM объект на пине 42
# Начальные параметры: частота 1000 Гц, скважность 50%
pwm = PWM(42, freq=1000, duty=50)

# Создаём объект для работы с кнопкой
key = YbKey()

# Переменная для хранения текущей скважности (яркости)
# Начинаем с 10%
duty = 10

# Бесконечный цикл программы
while True:
    # Проверяем, нажата ли кнопка
    if key.is_pressed():
        # Если кнопка нажата, увеличиваем скважность на 10%
        duty += 10
        
        # Если скважность превысила 100%, сбрасываем её в 0%
        # Это создаёт циклическое переключение: 10% → 20% → ... → 100% → 0% → 10% → ...
        if duty > 100:
            duty = 0
        
        # Устанавливаем новую скважность для PWM сигнала
        pwm.duty(duty)
        
        # Выводим текущее значение скважности в консоль
        print("Duty:", duty)
        
        # Ждём 300 миллисекунд, чтобы избежать多重ного срабатывания при одном нажатии
        # Это называется "антидребезг" (debounce)
        time.sleep_ms(300)
    
    # Небольшая задержка в основном цикле
    # Нужна, чтобы не перегружать процессор постоянными проверками
    time.sleep_ms(50)
