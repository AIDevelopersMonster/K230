# Пример вывода изображения с камеры с наложением текстовой информации
# Скрипт отображает название и счётчик кадров прямо на изображении
#
# Используется:
# - Sensor (камера)
# - Display (дисплей ST7701)
# - MediaManager (управление медиа)
#
