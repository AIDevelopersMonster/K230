# Описание:
# Работа с файловой системой: просмотр файлов, проверка существования,
# переименование и удаление файлов
#
# Используется:
# - Модуль os для работы с файловой системой
# - os.listdir() — получение списка файлов в директории
# - os.stat() — проверка существования файла
# - os.rename() — переименование файла
# - os.remove() — удаление файла
#
# ============================================

# Импортируем модуль os для работы с операционной системой и файловой системой
import os

# === Получение списка файлов ===
# os.listdir() возвращает список всех файлов и папок в указанной директории
# В данном случае смотрим содержимое SD-карты (/sdcard)
print('Files:', os.listdir('/sdcard'))

# === Проверка существования файла ===
# Используем try-except для безопасной проверки
# os.stat() получает информацию о файле, но выбрасывает ошибку, если файл не найден
try:
    os.stat('/sdcard/test.txt')
    print('File exists')  # Файл существует
except:
    print('File not found')  # Файл не найден

# === Переименование файла ===
# os.rename(старое_имя, новое_имя) переименовывает или перемещает файл
try:
    os.rename('/sdcard/test.txt', '/sdcard/test_renamed.txt')
    print('Renamed file')  # Файл успешно переименован
except:
    # Если файл не найден или произошла другая ошибка — ничего не делаем (pass)
    pass

# === Удаление файла ===
# os.remove() удаляет файл по указанному пути
try:
    os.remove('/sdcard/test_renamed.txt')
    print('File removed')  # Файл успешно удалён
except:
    # Если файл не найден или произошла другая ошибка — ничего не делаем (pass)
    pass
