# Описание:
# Базовый пример записи и чтения текстового файла на SD-карте
# Показывает, как создать файл, записать в него данные и прочитать обратно
#
# Используется:
# - Встроенная функция open() для работы с файлами
# - Конструкция with для автоматического закрытия файла
#
# ============================================

# Открываем файл для записи ('w' — write/запись)
# Если файла нет — он создастся, если есть — перезапишется
# Конструкция 'with' автоматически закроет файл после выхода из блока
with open('/sdcard/test.txt', 'w') as f:
    # Записываем строку 'Hello K230' в файл
    f.write('Hello K230')

# Выводим сообщение об успешной записи
print('File written')

# Открываем тот же файл для чтения ('r' — read/чтение)
with open('/sdcard/test.txt', 'r') as f:
    # Читаем всё содержимое файла в переменную data
    data = f.read()

# Выводим прочитанные данные на экран
print('Read data:', data)
