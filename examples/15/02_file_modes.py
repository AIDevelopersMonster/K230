# Описание:
# Демонстрация различных режимов работы с файлами: запись, добавление, чтение
# Показывает разницу между режимами 'w', 'a' и 'r'
#
# Используется:
# - Встроенная функция open() с разными режимами доступа
# - Метод write() для записи данных
# - Метод readlines() для чтения всех строк
#
# ============================================

# === Режим 'w' (write) — запись ===
# Открываем файл для записи. Если файл существует — он будет очищен (перезаписан)
# Если файла нет — он будет создан
with open('/sdcard/modes.txt', 'w') as f:
    # Записываем две строки в файл
    # \n — символ перехода на новую строку
    f.write('line1\n')
    f.write('line2\n')

# === Режим 'a' (append) — добавление ===
# Открываем файл для добавления данных в конец
# Существующее содержимое не удаляется, новые данные дописываются после старых
with open('/sdcard/modes.txt', 'a') as f:
    # Дописываем третью строку в конец файла
    f.write('line3\n')

# === Режим 'r' (read) — чтение ===
# Открываем файл только для чтения
with open('/sdcard/modes.txt', 'r') as f:
    # readlines() читает все строки файла и возвращает их как список
    # Цикл for перебирает каждую строку по очереди
    for line in f.readlines():
        # Выводим каждую строку с префиксом 'LINE:'
        # Обратите внимание: каждая строка уже содержит \n, поэтому пустых строк не будет
        print('LINE:', line)
