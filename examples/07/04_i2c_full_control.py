# Пример 4. UART команды → OLED + RGB + Буззер

# Импортируем необходимые модули:
# machine - базовые функции для работы с железом (I2C, FPIOA)
# ybUtils.ssd1306 - драйвер для OLED дисплея SSD1306
# ybUtils.YbUart - класс для работы с UART (последовательный порт)
# ybUtils.YbRGB - класс для управления RGB светодиодом
# ybUtils.YbBuzzer - класс для управления буззером (пищалкой)
# time - функции для работы со временем (задержки)
from machine import I2C, FPIOA
from ybUtils.ssd1306 import SSD1306_I2C
from ybUtils.YbUart import YbUart
from ybUtils.YbRGB import YbRGB
from ybUtils.YbBuzzer import YbBuzzer
import time

# Создаём объект для настройки пинов (FPIOA - Flexible Programmable Input/Output Array)
fpioa = FPIOA()
# Настраиваем пины для работы I2C:
# Пин 34 назначаем как SCL (тактовый сигнал)
# Пин 35 назначаем как SDA (линия данных)
fpioa.set_function(34, FPIOA.IIC1_SCL)
fpioa.set_function(35, FPIOA.IIC1_SDA)
# Создаём объект I2C с номером шины 1
i2c = I2C(1)



# Создаём объект OLED дисплея:
# 128 - ширина экрана в пикселях
# 32 - высота экрана в пикселях
# i2c - шина I2C для связи с дисплеем
oled = SSD1306_I2C(128, 32, i2c)

# Создаём объект для работы с UART:
# baudrate=115200 - скорость передачи данных (бит в секунду)
uart = YbUart(baudrate=115200)

# Создаём объект для управления RGB светодиодом
rgb = YbRGB()

# Создаём объект для управления буззером (звуковым излучателем)
buzzer = YbBuzzer()

# Бесконечный цикл для постоянного опроса UART команд
while True:
    # Читаем данные из UART порта
    data = uart.read()
    
    # Если данные получены (не пустые)
    if data:
        # Декодируем байты в строку, убираем пробелы и приводим к нижнему регистру
        cmd = data.decode().strip().lower()

        # Очищаем экран перед выводом новой информации
        oled.fill(0)
        
        # Выводим полученную команду на экран (максимум 16 символов)
        oled.text(cmd[:16], 0, 10)

        # Проверяем команду и выполняем соответствующее действие:
        if cmd == 'red':
            # Команда 'red' - включаем красный цвет
            # (255, 0, 0) - максимальная яркость красного, зелёный и синий выключены
            rgb.show_rgb((255, 0, 0))
        elif cmd == 'green':
            # Команда 'green' - включаем зелёный цвет
            # (0, 255, 0) - максимальная яркость зелёного, красный и синий выключены
            rgb.show_rgb((0, 255, 0))
        elif cmd == 'blue':
            # Команда 'blue' - включаем синий цвет
            # (0, 0, 255) - максимальная яркость синего, красный и зелёный выключены
            rgb.show_rgb((0, 0, 255))
        elif cmd == 'beep':
            # Команда 'beep' - издаём звуковой сигнал
            buzzer.beep()

        # Обновляем дисплей после всех изменений
        oled.show()

    # Делаем паузу 50 миллисекунд перед следующей проверкой команд
    time.sleep_ms(50)
