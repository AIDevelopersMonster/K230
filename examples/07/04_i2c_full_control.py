# Полный пример управления несколькими устройствами через UART команды.
# Принимает команды по UART и управляет OLED дисплеем, RGB светодиодом и буззером.
# Доступные команды: 'red', 'green', 'blue' (цвет светодиода), 'beep' (сигнал буззера).
#
# Используется:
# - YbUart / YbRGB / YbBuzzer / YbKey
