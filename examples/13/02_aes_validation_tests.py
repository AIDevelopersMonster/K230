# Описание:
# Проверка обработки ошибок в AES для K230.
# Демонстрирует как библиотека реагирует на:
# 1) неверную длину ключа (должен быть 16/24/32 байта)
# 2) неверную длину данных (должна быть кратна 16 байтам)
#
# Используется:
# - ucryptolib (библиотека криптографии)
# ============================================

# Импортируем библиотеку криптографии для работы с AES
import ucryptolib

# Тест 1: Проверка неверной длины ключа
# Ключ должен быть ровно 16, 24 или 32 байта для AES-128/192/256
# Пытаемся создать шифр с коротким ключом 'short_key' (9 байт)
print('Тест 1: Проверка неверной длины ключа...')
try:
    # Эта строка должна вызвать ошибку ValueError, т.к. ключ слишком короткий
    ucryptolib.aes(b'short_key', ucryptolib.MODE_ECB)
    # Если ошибки не произошло - это проблема, выбрасываем исключение
    raise Exception('Ошибка: неверная длина ключа НЕ обнаружена')
except ValueError:
    # Ожидаемая ошибка - ключ отвергнут из-за неправильной длины
    print('Неверная длина ключа: PASS (ошибка корректно обнаружена)')

# Тест 2: Проверка неверной длины данных
# Данные для шифрования должны быть кратны 16 байтам (размер блока AES)
print('Тест 2: Проверка неверной длины данных...')
try:
    # Создаём правильный ключ (16 байт)
    key = b'1234567890abcdef'
    # Создаём объект шифра с правильным ключом
    cipher = ucryptolib.aes(key, ucryptolib.MODE_ECB)
    # Пытаемся зашифровать данные длиной 5 байт (не кратно 16)
    # Это должно вызвать ошибку ValueError
    cipher.encrypt(b'short')
    # Если ошибки не произошло - это проблема
    raise Exception('Ошибка: неверная длина данных НЕ обнаружена')
except ValueError:
    # Ожидаемая ошибка - данные отвергнуты из-за неправильной длины
    print('Неверная длина данных: PASS (ошибка корректно обнаружена)')

# Все тесты пройдены успешно
print('Все тесты пройдены: PASS')
