# Описание:
# Демонстрация уязвимости режима ECB в AES.
# Показывает что одинаковые блоки открытого текста
# дают одинаковые блоки шифротекста, что является
# серьёзной проблемой безопасности ECB режима.
#
# Используется:
# - ucryptolib (библиотека криптографии)
# ============================================

# Импортируем библиотеку криптографии для работы с AES
import ucryptolib

# Импортируем модуль для работы с шестнадцатеричным представлением
# Сначала пробуем стандартный binascii, если не работает - используем ubinascii
try:
    import binascii
except ImportError:
    import ubinascii as binascii


def to_hex(data):
    """
    Преобразует байтовые данные в шестнадцатеричную строку.
    
    Args:
        data: байтовые данные для преобразования
    
    Returns:
        Строка с шестнадцатеричным представлением данных
    """
    # binascii.hexlify возвращает байты, поэтому декодируем в строку
    value = binascii.hexlify(data)
    if isinstance(value, bytes):
        return value.decode()
    return value


# Ключ шифрования - 16 байт для AES-128
key = b'1234567890abcdef'

# Создаём два одинаковых блока данных по 16 байт каждый
# 'A' повторяется 16 раз - это один блок
block = b'AAAAAAAAAAAAAAAA'
# Соединяем два одинаковых блока вместе (итого 32 байта)
data = block + block

# Создаём объект шифра с ключом и режимом ECB
# ECB (Electronic Codebook) - каждый блок шифруется независимо
cipher = ucryptolib.aes(key, ucryptolib.MODE_ECB)

# Шифруем данные (два одинаковых блока)
encrypted = cipher.encrypt(data)

# Разделяем зашифрованные данные на два блока по 16 байт
# Первый зашифрованный блок
block1 = encrypted[:16]
# Второй зашифрованный блок
block2 = encrypted[16:32]

# Выводим результаты в шестнадцатеричном формате для наглядности
print('Блок 1 (hex) =', to_hex(block1))
print('Блок 2 (hex) =', to_hex(block2))

# Проверяем равны ли зашифрованные блоки
# В режиме ECB они БУДУТ равны, так как исходные блоки одинаковые
print('Блоки равны:', block1 == block2)

# Выводим заключение о демонстрации уязвимости ECB
print('\nДемонстрация уязвимости ECB:')
print('Одинаковые блоки открытого текста -> одинаковые блоки шифротекста')
print('Это позволяет злоумышленнику анализировать паттерны в данных!')
print('Для реальных приложений используйте CBC, CTR или GCM режимы.')
