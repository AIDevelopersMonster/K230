# Пример 1. Базовое управление RGB-светодиодом на YAHBOOM K230
#
# Этот пример показывает самый простой способ включить RGB-светодиод,
# подождать несколько секунд и затем выключить его.
#
# Основано на примере из документации RGB Lights:
# используется библиотека YbRGB и метод show_rgb((R, G, B)).

from ybUtils.YbRGB import YbRGB
import time

# Создаём объект для управления встроенным RGB-светодиодом
rgb = YbRGB()

# Включаем мягкий синий цвет
# Формат цвета: (R, G, B), где каждое значение от 0 до 255
rgb.show_rgb((82, 139, 255))

# Держим цвет 3 секунды
time.sleep(3)

# Выключаем светодиод
rgb.show_rgb((0, 0, 0))
