# Описание:
# Базовое отображение изображения из файла на экран дисплея.
# Скрипт загружает PNG-изображение с SD-карты, конвертирует его
# в формат RGB888 и выводит на экран.
#
# Используется:
# - Display (дисплей ST7701)
# - MediaManager (управление медиа)
# - image (загрузка и обработка изображений)
#
# ============================================

# Импорт необходимых библиотек для работы с дисплеем и медиа
from media.display import *
from media.media import *
import image
import time

# ============================================
# Инициализация оборудования
# ============================================

# Инициализация дисплея с параметрами:
# - Display.ST7701: тип контроллера дисплея
# - width=640, height=480: разрешение экрана в пикселях
# - osd_num=1: количество слоёв OSD (On-Screen Display)
# - to_ide=True: режим работы с IDE для отладки
Display.init(Display.ST7701, width=640, height=480, osd_num=1, to_ide=True)

# Инициализация менеджера медиа - необходим для работы с файлами и изображениями
MediaManager.init()

# ============================================
# Загрузка и обработка изображения
# ============================================

# Загружаем изображение из файла на SD-карте
# Параметры:
# - '/sdcard/resources/wp.png': путь к файлу изображения
# - copy_to_fb=True: копировать изображение в кадровый буфер (framebuffer)
#   Это нужно для быстрой отрисовки на экране
img = image.Image('/sdcard/resources/wp.png', copy_to_fb=True)

# Конвертируем изображение в формат RGB888
# PNG файлы могут быть в разных форматах, а дисплей требует RGB888
# to_rgb888() преобразует изображение в 24-битный цвет (по 8 бит на красный, зелёный, синий)
img = img.to_rgb888()

# ============================================
# Отображение изображения
# ============================================

# Выводим обработанное изображение на экран
# show_image() принимает объект изображения и отображает его на дисплее
Display.show_image(img)

# Выводим сообщение в консоль об успешном отображении
print('Image displayed: /sdcard/resources/wp.png')

# ============================================
# Основной цикл программы
# ============================================

# Бесконечный цикл для поддержания работы программы
# Без этого цикла программа завершится и изображение пропадёт
# time.sleep(1) приостанавливает выполнение на 1 секунду в каждой итерации,
# чтобы не нагружать процессор
while True:
    time.sleep(1)
