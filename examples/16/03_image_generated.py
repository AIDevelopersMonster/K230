# Описание:
# Генерация изображения с текстом поверх загруженной картинки.
# Скрипт демонстрирует, как добавить текст на изображение
# с помощью метода draw_string_advanced() и отобразить результат.
#
# Используется:
# - Display (дисплей ST7701)
# - MediaManager (управление медиа)
# - image (загрузка и обработка изображений)
#
# ============================================

# Импорт необходимых библиотек для работы с дисплеем и медиа
from media.display import *
from media.media import *
import image
import time

# ============================================
# Инициализация оборудования
# ============================================

# Инициализация дисплея с параметрами:
# - Display.ST7701: тип контроллера дисплея
# - width=640, height=480: разрешение экрана в пикселях
# - osd_num=1: количество слоёв OSD (On-Screen Display)
# - to_ide=True: режим работы с IDE для отладки
Display.init(Display.ST7701, width=640, height=480, osd_num=1, to_ide=True)

# Инициализация менеджера медиа - необходим для работы с файлами и изображениями
MediaManager.init()

# ============================================
# Загрузка и подготовка изображения
# ============================================

# Загружаем фоновое изображение из файла на SD-карте
# Параметры:
# - '/sdcard/resources/wp.png': путь к файлу изображения
# - copy_to_fb=True: копировать изображение в кадровый буфер (framebuffer)
#   Это позволяет рисовать прямо на изображении перед отображением
img = image.Image('/sdcard/resources/wp.png', copy_to_fb=True)

# Конвертируем изображение в формат RGB888
# PNG файлы могут быть в разных форматах, а дисплей требует RGB888
# to_rgb888() преобразует изображение в 24-битный цвет
img = img.to_rgb888()

# ============================================
# Добавление текста на изображение
# ============================================

# Метод draw_string_advanced() позволяет рисовать текст на изображении
# Параметры метода:
# - x, y: координаты левого верхнего угла текста (в пикселях)
# - size: размер шрифта
# - text: строка текста для отображения
# - color: цвет текста в формате (R, G, B), где каждое значение от 0 до 255

# Первая надпись: "Hello K230"
# - Координаты: (30, 30) - отступ 30 пикселей от левого и верхнего края
# - Размер шрифта: 28
# - Цвет: (255, 255, 0) - жёлтый (максимум красного + зелёного, без синего)
img.draw_string_advanced(30, 30, 28, "Hello K230", color=(255, 255, 0))

# Вторая надпись: "wp.png demo"
# - Координаты: (30, 70) - ниже первой надписи
# - Размер шрифта: 20 (меньше первой)
# - Цвет: (0, 255, 255) - циан/голубой (максимум зелёного + синего, без красного)
img.draw_string_advanced(30, 70, 20, "wp.png demo", color=(0, 255, 255))

# ============================================
# Отображение изображения
# ============================================

# Показываем изображение с нанесённым текстом на экране
Display.show_image(img)

# Выводим сообщение в консоль об успешном отображении
print("Image with text displayed")

# ============================================
# Основной цикл программы
# ============================================

# Бесконечный цикл для поддержания работы программы
# Без этого цикла программа завершится и изображение пропадёт
# time.sleep(1) приостанавливает выполнение на 1 секунду в каждой итерации,
# чтобы не нагружать процессор
while True:
    time.sleep(1)
