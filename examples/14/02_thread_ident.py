# Описание:
# Демонстрация идентификаторов потоков и
# параллельного выполнения.
# Показывает, как получить уникальный ID каждого потока
# и как потоки работают одновременно.
#
# Используется:
# - _thread
# - time
# ============================================

import _thread
import time


def worker():
    """
    Функция, которая выполняется в каждом потоке.
    Получает свой уникальный идентификатор потока и ведёт счётчик.
    """
    # Получаем уникальный идентификатор текущего потока
    thread_id = _thread.get_ident()
    counter = 0

    # Бесконечный цикл - поток работает постоянно
    while True:
        # Выводим номер потока и текущее значение счётчика
        print("Thread ID: {} | counter: {}".format(thread_id, counter))
        counter += 1

        # Важно: даём планировщику шанс переключиться на другой поток
        # sleep_ms() - задержка в миллисекундах (если доступна)
        # sleep() - задержка в секундах
        if hasattr(time, "sleep_ms"):
            time.sleep_ms(500)  # 500 мс = 0.5 секунды
        else:
            time.sleep(0.5)


# Запускаем первый поток
# worker, () - функция без аргументов
_thread.start_new_thread(worker, ())

# Запускаем второй поток (тоже функция worker)
_thread.start_new_thread(worker, ())

# Основной (главный) поток должен работать, иначе программа завершится
# Он ничего не делает, просто поддерживает программу живой
while True:
    # Короткая задержка, чтобы не занимать процессор полностью
    if hasattr(time, "sleep_ms"):
        time.sleep_ms(1)
    else:
        time.sleep(0.001)
