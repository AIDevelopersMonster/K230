# Описание:
# Базовый пример многопоточности для K230.
# Показывает, как запустить два потока через _thread,
# чтобы они по очереди печатали сообщения в терминал.
#
# Используется:
# - _thread
# - time
# ============================================

import _thread
import time


def worker(name):
    """
    Функция, которая выполняется в каждом потоке.
    
    Аргументы:
        name - имя потока (строка для отображения в выводе)
    """
    # Бесконечный цикл - поток работает постоянно
    while True:
        # Выводим сообщение с именем потока
        print("This is thread {}".format(name))
        
        # Важно: даём планировщику шанс переключить поток
        # Без sleep() этот поток займёт 100% процессора
        # и другие потоки не смогут выполняться
        time.sleep(1)


# Запускаем первый поток с именем THREAD_1
# _thread.start_new_thread(функция, кортеж_аргументов)
_thread.start_new_thread(worker, ("THREAD_1",))

# Небольшая задержка, чтобы первый поток успел стартовать
# Это нужно для демонстрации - чтобы увидеть разницу во времени запуска
if hasattr(time, "sleep_ms"):
    time.sleep_ms(500)  # 500 миллисекунд = 0.5 секунды
else:
    time.sleep(0.5)  # 0.5 секунды

# Запускаем второй поток с именем THREAD_2
_thread.start_new_thread(worker, ("THREAD_2",))

# Главный поток должен работать бесконечно
# Если главный поток завершится, программа остановится
# и все дочерние потоки будут убиты
while True:
    # Короткая задержка, чтобы не занимать процессор полностью
    # Даём возможность другим потокам выполняться
    if hasattr(time, "sleep_ms"):
        time.sleep_ms(1)  # 1 миллисекунда
    else:
        time.sleep(0.001)  # 0.001 секунды = 1 миллисекунда
