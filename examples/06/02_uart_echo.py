from ybUtils.YbUart import YbUart  # Импорт класса для работы с UART
import time  # Импорт модуля времени для задержек

# Создаем объект UART с скоростью передачи 115200 бит/сек
uart = YbUart(baudrate=115200)

# Выводим сообщение о запуске программы
print("UART Echo запущен")

# Бесконечный цикл для постоянного чтения и отправки данных
while True:
    # Читаем данные из UART (если что-то пришло)
    data = uart.read()
    
    # Если данные получены (не пустые)
    if data:
        # Декодируем байты в строку
        text = data.decode()
        
        # Выводим полученное сообщение на экран
        print("Получено:", text)
        
        # Отправляем обратно то же сообщение с приставкой "Echo: "
        uart.send("Echo: " + text)
    
    # Небольшая задержка 50 мс, чтобы не перегружать процессор
    time.sleep_ms(50)
