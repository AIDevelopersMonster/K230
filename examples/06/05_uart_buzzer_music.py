# Описание:
# Управление буззером (пищалкой) через UART команды
# Отправьте команды: beep (одиночный сигнал) 
# или alarm (сирена из трёх гудков)
#
# Используется:
# - YbUart / YbBuzzer
#
# ============================================

from ybUtils.YbUart import YbUart      # Импорт класса для работы с UART
from ybUtils.YbBuzzer import YbBuzzer  # Импорт класса для управления буззером
import time  # Импорт модуля времени для задержек

# Создаем объект UART с скоростью передачи 115200 бит/сек
uart = YbUart(baudrate=115200)

# Создаем объект для управления буззером (звуковым сигнализатором)
buzzer = YbBuzzer()

# Выводим подсказку о доступных командах
print("Команды: beep / alarm")

# Бесконечный цикл для постоянного чтения команд из UART
while True:
    # Читаем данные из UART (если что-то пришло)
    data = uart.read()
    
    # Если данные получены (не пустые)
    if data:
        # Декодируем байты в строку, убираем пробелы по краям и приводим к нижнему регистру
        cmd = data.decode().strip().lower()

        # Проверяем команду и выполняем соответствующее действие
        if cmd == "beep":
            # Одиночный короткий сигнал
            buzzer.beep()
        elif cmd == "alarm":
            # Сирена: три гудка подряд
            # Повторяем 3 раза
            for _ in range(3):
                # Включаем буззер на 1000 Гц, громкость 50%, длительность 0.1 сек
                buzzer.on(1000, 50, 0.1)
                # Пауза между гудками 0.1 сек
                time.sleep(0.1)

        # Выводим полученную команду на экран для отладки
        print("CMD:", cmd)

    # Небольшая задержка 50 мс между проверками
    time.sleep_ms(50)
