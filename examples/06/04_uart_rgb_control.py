# Описание:
# Управление RGB светодиодом через UART команды
# Отправьте команды: red / green / blue / off
# для изменения цвета светодиода
#
# Используется:
# - YbUart / YbRGB
#
# ============================================

from ybUtils.YbUart import YbUart  # Импорт класса для работы с UART
from ybUtils.YbRGB import YbRGB    # Импорт класса для управления RGB светодиодом
import time  # Импорт модуля времени для задержек

# Создаем объект UART с скоростью передачи 115200 бит/сек
uart = YbUart(baudrate=115200)

# Создаем объект для управления RGB светодиодом
rgb = YbRGB()

# Выводим подсказку о доступных командах
print("Отправь: red / green / blue / off")

# Бесконечный цикл для постоянного чтения команд из UART
while True:
    # Читаем данные из UART (если что-то пришло)
    data = uart.read()
    
    # Если данные получены (не пустые)
    if data:
        # Декодируем байты в строку, убираем пробелы по краям и приводим к нижнему регистру
        cmd = data.decode().strip().lower()

        # Проверяем команду и устанавливаем соответствующий цвет
        if cmd == "red":
            # Красный цвет: (255, 0, 0) - максимальная яркость красного, остальные выключены
            rgb.show_rgb((255, 0, 0))
        elif cmd == "green":
            # Зеленый цвет: (0, 255, 0) - максимальная яркость зеленого
            rgb.show_rgb((0, 255, 0))
        elif cmd == "blue":
            # Синий цвет: (0, 0, 255) - максимальная яркость синего
            rgb.show_rgb((0, 0, 255))
        elif cmd == "off":
            # Выключить светодиод: (0, 0, 0) - все цвета на минимум
            rgb.show_rgb((0, 0, 0))

        # Выводим полученную команду на экран для отладки
        print("CMD:", cmd)

    # Небольшая задержка 50 мс между проверками
    time.sleep_ms(50)
