# Пример 6. WDT + OLED UI для K230
#
# Что делает программа:
# - запускает watchdog
# - показывает состояние системы на OLED
# - показывает статус кнопки
# - показывает наличие UART-активности
# - при получении данных по UART кормит watchdog
# - при нажатии кнопки тоже кормит watchdog
# - если долго нет активности, watchdog перезапустит систему

# Импортируем необходимые модули из machine:
# WDT - сторожевой таймер для защиты от зависаний
# I2C - интерфейс для подключения OLED дисплея
# FPIOA - модуль для настройки пинов платы
from machine import WDT, I2C, FPIOA
# Импортируем драйвер для OLED дисплея SSD1306
from ybUtils.ssd1306 import SSD1306_I2C
# Импортируем класс YbKey для работы с кнопкой
from ybUtils.YbKey import YbKey
# Импортируем класс YbUart для работы с UART интерфейсом
from ybUtils.YbUart import YbUart
# Импортируем модуль time для работы с временем и задержками
import time

# -------------------------------
# Настройки
# -------------------------------
# ID сторожевого таймера
WDT_ID = 1
# Таймаут WDT в секундах (если не покормить за это время - будет перезагрузка)
WDT_TIMEOUT = 3
# Таймаут UART в секундах (если дольше нет данных по UART - считаем канал неактивным)
UART_TIMEOUT = 2

# -------------------------------
# Инициализация OLED (I2C1)
# -------------------------------
# Создаем объект FPIOA для настройки пинов
fpioa = FPIOA()
# Настраиваем пины для I2C интерфейса:
# Пин 34 - SCL (тактовый сигнал), пин 35 - SDA (данные)
# Параметры: oe=1 (выход включен), ie=1 (вход включен), pu=1 (подтяжка), st=1 (триггер Шмитта), ds=15 (сила драйвера)
fpioa.set_function(34, FPIOA.IIC1_SCL, oe=1, ie=1, pu=1, st=1, ds=15)
fpioa.set_function(35, FPIOA.IIC1_SDA, oe=1, ie=1, pu=1, st=1, ds=15)

# Создаем объект I2C с номером 1
i2c = I2C(1)

# Создаем объект OLED дисплея:
# 128x32 - разрешение дисплея в пикселях
# i2c - интерфейс для связи
oled = SSD1306_I2C(128, 32, i2c)

# -------------------------------
# Инициализация устройств
# -------------------------------
# Создаем объект WDT с указанным ID и таймаутом
wdt = WDT(WDT_ID, WDT_TIMEOUT)
# Создаем объект для работы с кнопкой
key = YbKey()
# Создаем объект для работы с UART со скоростью 115200 бит/сек
uart = YbUart(baudrate=115200)

# -------------------------------
# Состояние системы
# -------------------------------
# Время последнего получения данных по UART (используется для отслеживания активности)
last_uart_time = time.time()
# Текст последнего полученного сообщения (по умолчанию "none")
last_uart_text = "none"
# Счетчик количества "кормлений" watchdog
feed_count = 0
# Источник последнего "кормления" (при старте - "boot")
last_source = "boot"


def draw_line(y):
    """
    Рисует горизонтальную линию на OLED дисплее.
    
    Аргументы:
        y - координата Y где рисовать линию
    
    Примечание:
        На некоторых версиях SSD1306 нет функции hline(),
        поэтому рисуем линию вручную через pixel().
    """
    # Проходим по всем точкам строки от 0 до 127
    for x in range(128):
        # Рисуем точку с координатами (x, y) яркостью 1 (включено)
        oled.pixel(x, y, 1)


def draw_ui(button_state, uart_ok, seconds_left):
    """
    Отрисовывает пользовательский интерфейс на OLED дисплее.
    
    Аргументы:
        button_state - статус кнопки ("ON" или "OFF")
        uart_ok - статус UART ("OK" или "WAIT")
        seconds_left - оставшееся время до потенциального сброса
    """
    # Очищаем весь экран (заполняем черным цветом)
    oled.fill(0)

    # Заголовок - пишем текст "WDT OLED UI" в координатах (0, 0)
    oled.text("WDT OLED UI", 0, 0)
    # Рисуем разделительную линию под заголовком
    draw_line(9)

    # Строка 1: Статус кнопки и UART
    # Показываем статус кнопки в левой части экрана
    oled.text("BTN:" + button_state, 0, 12)
    # Показываем статус UART в правой части экрана
    oled.text("UART:" + uart_ok, 64, 12)

    # Строка 2: Счетчик кормлений и оставшееся время
    # Показываем количество "кормлений" watchdog
    oled.text("Feed:" + str(feed_count), 0, 22)
    # Показываем оставшееся время (T:) в правой части
    oled.text("T:" + str(seconds_left), 80, 22)

    # Обновляем дисплей - отправляем буфер на экран
    oled.show()


# Выводим стартовые сообщения в консоль
print("WDT + OLED UI started")
print("UART heartbeat timeout:", UART_TIMEOUT, "sec")
print("WDT timeout:", WDT_TIMEOUT, "sec")

# Основной бесконечный цикл программы
while True:
    # Получаем текущее время в секундах
    now = time.time()

    # -------------------------------
    # Читаем кнопку
    # -------------------------------
    # Проверяем, нажата ли кнопка
    if key.is_pressed():
        # Кнопка нажата - устанавливаем статус "ON"
        button_state = "ON"
        # "Кормим" сторожевой таймер
        wdt.feed()
        # Увеличиваем счетчик кормлений
        feed_count += 1
        # Запоминаем что последнее кормление было от кнопки
        last_source = "button"
        # Выводим сообщение в консоль
        print("Feed by button")
        # Ждем 250 миллисекунд чтобы избежать множественных срабатываний (антидребезг)
        time.sleep_ms(250)
    else:
        # Кнопка не нажата - устанавливаем статус "OFF"
        button_state = "OFF"

    # -------------------------------
    # Читаем UART
    # -------------------------------
    # Читаем данные из UART порта
    data = uart.read()
    # Если данные получены
    if data:
        try:
            # Пытаемся декодировать полученные данные в строку
            last_uart_text = data.decode().strip()
        except:
            # Если декодирование не удалось - пишем ошибку
            last_uart_text = "decode err"

        # Запоминаем время получения данных
        last_uart_time = now
        # "Кормим" сторожевой таймер
        wdt.feed()
        # Увеличиваем счетчик кормлений
        feed_count += 1
        # Запоминаем что последнее кормление было от UART
        last_source = "uart"
        # Выводим сообщение в консоль с полученными данными
        print("Feed by UART:", last_uart_text)

    # -------------------------------
    # Определяем статус UART
    # -------------------------------
    # Вычисляем сколько времени прошло с последнего получения данных
    uart_age = now - last_uart_time
    # Если прошло меньше UART_TIMEOUT секунд - статус OK, иначе WAIT
    uart_ok = "OK" if uart_age < UART_TIMEOUT else "WAIT"

    # Вычисляем сколько примерно осталось до потенциального сброса
    # Берем максимальное значение между 0 и разницей таймаута и возраста UART
    seconds_left = int(max(0, WDT_TIMEOUT - uart_age))

    # Обновляем OLED дисплей с текущими данными
    draw_ui(button_state, uart_ok, seconds_left)

    # Ждем 0.1 секунды перед следующей итерацией цикла
    time.sleep(0.1)
