# Пример 4. UART кормит WDT

# Импортируем модуль WDT из machine для работы со сторожевым таймером
from machine import WDT
# Импортируем класс YbUart для работы с UART интерфейсом
from ybUtils.YbUart import YbUart
# Импортируем модуль time для работы с задержками
import time

# Создаем объект WDT:
# Первый параметр (1) - ID сторожевого таймера
# Второй параметр (3) - таймаут в секундах
wdt = WDT(1, 3)

# Создаем объект для работы с UART
# Указываем скорость передачи данных 115200 бит/сек (стандартная скорость)
uart = YbUart(115200)

# Бесконечный цикл программы
while True:
    # Читаем данные из UART порта
    # read() возвращает полученные данные или None если данных нет
    data = uart.read()
    
    # Если данные получены (data не пустое)
    if data:
        # Выводим сообщение в консоль о кормлении через UART
        print("Feed via UART")
        # "Кормим" сторожевой таймер - сбрасываем его отсчет
        wdt.feed()
    
    # Ждем 0.1 секунды перед следующей проверкой UART
    # Это нужно для стабильной работы и предотвращения перегрузки CPU
    time.sleep(0.1)
