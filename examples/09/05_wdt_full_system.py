# Пример 5. Полная система (кнопка + UART + WDT)

# Импортируем модуль WDT из machine для работы со сторожевым таймером
from machine import WDT
# Импортируем класс YbKey для работы с кнопкой на плате Yahboom
from ybUtils.YbKey import YbKey
# Импортируем класс YbUart для работы с UART интерфейсом
from ybUtils.YbUart import YbUart
# Импортируем модуль time для работы с задержками
import time

# Создаем объект WDT:
# Первый параметр (1) - ID сторожевого таймера
# Второй параметр (3) - таймаут в секундах
wdt = WDT(1, 3)

# Создаем объект для работы с кнопкой
key = YbKey()

# Создаем объект для работы с UART
# Скорость передачи данных 115200 бит/сек
uart = YbUart(115200)

# Бесконечный цикл программы
while True:
    # Проверяем, нажата ли кнопка
    if key.is_pressed():
        # Если кнопка нажата - выводим сообщение в консоль
        print("Feed by button")
        # "Кормим" сторожевой таймер
        wdt.feed()

    # Читаем данные из UART порта
    data = uart.read()
    # Если данные получены
    if data:
        # Выводим сообщение в консоль
        print("Feed by UART")
        # "Кормим" сторожевой таймер
        wdt.feed()

    # Ждем 0.1 секунды перед следующей проверкой
    # Это нужно для стабильной работы и предотвращения перегрузки CPU
    time.sleep(0.1)
