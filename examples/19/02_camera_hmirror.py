# Описание:
# Пример работы с горизонтальным отражением камеры на K230.
# Скрипт захватывает изображение с камеры, применяет горизонтальное
# зеркальное отражение и отображает результат на дисплее.
#
# Используется:
# - Camera Sensor (камера)
# - Display ST7701 (дисплей)
# - MediaManager (управление медиа-ресурсами)
# ============================================

# Импортируем необходимые модули
import uos as os          # Модуль для системных операций (например, выход из программы)
import time               # Модуль для работы со временем (задержки)
from media.sensor import *   # Модуль для управления камерой (сенсором изображения)
from media.display import *  # Модуль для управления дисплеем
from media.media import *    # Модуль для управления медиа-ресурсами (память, буферы)


def init_sensor():
    """
    Функция инициализации камеры с горизонтальным отражением.
    
    Что делает:
    1. Создаёт объект камеры
    2. Сбрасывает настройки камеры к заводским
    3. Устанавливает разрешение изображения (640x480 пикселей)
    4. Устанавливает формат цвета RGB565 (16 бит на пиксель)
    5. Включает горизонтальное зеркальное отражение
    
    Возвращает:
        sensor - настроенный объект камеры с включенным hmirror
    """
    # Создаём объект камеры
    sensor = Sensor()
    
    # Сбрасываем камеру - это важно для применения новых настроек
    sensor.reset()
    
    # Устанавливаем разрешение изображения:
    # width=640 - ширина в пикселях
    # height=480 - высота в пикселях
    # chn=CAM_CHN_ID_1 - используем канал 1 (основной канал для обработки)
    sensor.set_framesize(width=640, height=480, chn=CAM_CHN_ID_1)
    
    # Устанавливаем формат цвета:
    # Sensor.RGB565 - 16-битный формат цвета (красный 5 бит, зелёный 6 бит, синий 5 бит)
    # chn=CAM_CHN_ID_1 - применяем к каналу 1
    sensor.set_pixformat(Sensor.RGB565, chn=CAM_CHN_ID_1)

    # Включаем горизонтальное зеркальное отражение (hmirror)
    # True - отражение включено, изображение будет зеркальным по горизонтали
    # Это полезно, когда камера установлена "задом наперёд" или для селфи-режима
    sensor.set_hmirror(True)

    # Возвращаем настроенный объект камеры
    return sensor


def main():
    """
    Основная функция программы.
    
    Логика работы:
    1. Инициализируем камеру с горизонтальным отражением
    2. Инициализируем дисплей
    3. Запускаем медиа-менеджер
    4. В бесконечном цикле захватываем кадры и показываем их на дисплее
    5. При прерывании корректно освобождаем ресурсы
    """
    # Переменная для хранения объекта камеры
    # Изначально равна None (пусто)
    sensor = None
    
    try:
        # Настраиваем и инициализируем камеру (с hmirror)
        sensor = init_sensor()
        
        # Инициализируем дисплей:
        # Display.ST7701 - тип дисплея (контроллер ST7701)
        # width=640, height=480 - разрешение дисплея
        # to_ide=True - разрешаем вывод в IDE для отладки
        Display.init(Display.ST7701, width=640, height=480, to_ide=True)
        
        # Инициализируем медиа-менеджер
        # Он управляет памятью и буферами для передачи данных между камерой и дисплеем
        MediaManager.init()
        
        # Запускаем камеру - начинаем захват изображений
        sensor.run()

        # Основной бесконечный цикл программы
        while True:
            # Проверяем, не было ли сигнала прерывания от пользователя
            # Это позволяет корректно выйти из цикла при нажатии Ctrl+C
            os.exitpoint()
            
            # Делаем снимок (захватываем текущий кадр с камеры)
            # chn=CAM_CHN_ID_1 - берём изображение из канала 1
            img = sensor.snapshot(chn=CAM_CHN_ID_1)
            
            # Показываем полученное изображение на дисплее
            # Изображение уже будет с применённым горизонтальным отражением
            Display.show_image(img)

    except KeyboardInterrupt:
        # Обработка прерывания от пользователя (Ctrl+C)
        print("User interrupted the program")
    except Exception as e:
        # Обработка любых других ошибок
        print("An error occurred: {}".format(e))
    finally:
        # Блок finally выполняется всегда, даже если была ошибка
        # Здесь мы освобождаем ресурсы
        
        # Останавливаем камеру, если она была создана
        if isinstance(sensor, Sensor):
            sensor.stop()
        
        # Деинициализируем дисплей (выключаем)
        Display.deinit()
        
        # Разрешаем системе перейти в режим сна
        os.exitpoint(os.EXITPOINT_ENABLE_SLEEP)
        
        # Небольшая задержка перед завершением (100 миллисекунд)
        time.sleep_ms(100)
        
        # Освобождаем медиа-ресурсы (память, буферы)
        MediaManager.deinit()


# Точка входа в программу
# Код внутри этого блока выполнится только если файл запущен напрямую
if __name__ == "__main__":
    main()
