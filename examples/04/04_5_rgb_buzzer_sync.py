# Пример 5. Синхронизация RGB и буззера на YAHBOOM K230
#
# В этом примере свет и звук работают вместе:
# - каждой ноте соответствует свой цвет
# - во время проигрывания звука RGB-светодиод загорается
# - между нотами можно делать небольшие паузы
#
# Такой подход хорошо подходит для:
# - обучающих проектов
# - светозвуковой индикации
# - игровых и интерактивных устройств

from ybUtils.YbBuzzer import YbBuzzer
from ybUtils.YbRGB import YbRGB
import time

# Создаём объекты управления
buzzer = YbBuzzer()
rgb = YbRGB()

# Частоты нот (Гц)
NOTES = {
    "C4": 262,
    "D4": 294,
    "E4": 330,
    "F4": 349,
    "G4": 392,
    "A4": 440,
    "B4": 494,
    "C5": 523,
    "D5": 587,
    "E5": 659,
    "F5": 698,
    "G5": 784,
    "A5": 880,
    "B5": 988,
    "P": 0,  # Пауза
}

# Цвет для каждой ноты
NOTE_COLORS = {
    "C4": (255, 0, 0),
    "D4": (255, 127, 0),
    "E4": (255, 255, 0),
    "F4": (0, 255, 0),
    "G4": (0, 255, 255),
    "A4": (0, 0, 255),
    "B4": (139, 0, 255),
    "C5": (255, 0, 0),
    "D5": (255, 127, 0),
    "E5": (255, 255, 0),
    "F5": (0, 255, 0),
    "G5": (0, 255, 255),
    "A5": (0, 0, 255),
    "B5": (139, 0, 255),
    "P": (0, 0, 0),
}


def play_note_with_light(note, duration, volume=50, gap=0.05):
    """Проигрывает ноту и одновременно включает соответствующий цвет."""
    freq = NOTES.get(note, 0)
    color = NOTE_COLORS.get(note, (255, 255, 255))

    if freq == 0:
        # Пауза: выключаем и звук, и свет
        buzzer.off()
        rgb.show_rgb((0, 0, 0))
        time.sleep(duration)
    else:
        # Включаем цвет для текущей ноты
        rgb.show_rgb(color)
        # Воспроизводим звук
        buzzer.on(freq, volume, duration)

    # После ноты делаем короткую паузу и выключаем свет
    rgb.show_rgb((0, 0, 0))
    time.sleep(gap)


def play_melody_with_light(melody, volume=50):
    """Проигрывает мелодию со световой индикацией."""
    for note, duration in melody:
        play_note_with_light(note, duration, volume)

    buzzer.off()
    rgb.show_rgb((0, 0, 0))


# Небольшая демонстрационная мелодия
MELODY = [
    ("C5", 0.25), ("D5", 0.25), ("E5", 0.25), ("F5", 0.25),
    ("G5", 0.25), ("A5", 0.25), ("B5", 0.25), ("C5", 0.5),
    ("P", 0.2),
    ("G5", 0.25), ("E5", 0.25), ("C5", 0.5),
]


if __name__ == "__main__":
    print("Запуск светозвуковой демонстрации...")
    play_melody_with_light(MELODY, volume=50)
