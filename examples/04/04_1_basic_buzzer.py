# Пример 1. Базовая работа с буззером на YAHBOOM K230
#
# В этом примере показаны три простых сценария:
# 1) короткий сигнал стандартными параметрами,
# 2) сигнал с заданной частотой и длительностью,
# 3) простой тревожный сигнал.

from ybUtils.YbBuzzer import YbBuzzer
import time

# Создаём объект для управления буззером
buzzer = YbBuzzer()

# Пример 1. Короткий стандартный сигнал
buzzer.beep()
time.sleep(3)

# Пример 2. Пользовательские параметры:
# on(частота_Гц, громкость_проценты, длительность_сек)
buzzer.on(2000, 50, 0.5)
time.sleep(3)

# Пример 3. Тревожный сигнал из трёх коротких звуков
for _ in range(3):
    buzzer.on(1000, 50, 0.1)
    time.sleep(0.1)

# На всякий случай выключаем буззер
buzzer.off()
